                      <h1 class="blogh1">Skin grafting helps you heal and restore after a traumatic injury.
</h1>
                    
                     <p>The skin grafting procedure has come of age and is progressively being utilized outside the realm of cosmetic and reconstructive facial surgery; especially for managing wounds and skin loss in the trauma patients. </p>
 <p>                    
For example, if someone has been treated for wound, fracture, infection or burn that has resulted in some amount of skin loss/defect, skin grafting can be a good alternative option to restore the original skin quality.</p>
<p>
Other examples include venous ulcers, pressure ulcers(bedsores), or diabetic ulcers that do not heal with normal treatment. Skin grafting is often used after the mastectomy and skin cancer surgeries that require skin reconstruction to boost healing of wounds and to enable postoperative radiotherapy.
</p>





<h2 class="blogh2">
Skin grafts procedure</h2>
<p>
 The fundamental of skin graft surgery involves covering the area with no skin or damaged skin with healthy skin. The healthy skin can be taken from another body part of the patient or from a cadaver. Even artificial skin is also possible.</p>
 <p> 
The property of skin tissues that allows them to adhere and grow at any place of the body provides the basis for the high success rate of skin graft surgery. Ample coverage of the excised area is performed where tiny blood vessels connect and grow into the dermis of the graft.
</p>

<h2 class="blogh2">Skin graft types
</h2>
<p>A skin graft procedure can be classified as either of the below types:<br>
1. Partial or Split Thickness Skin Graft  or STSG. <br>
2. Full Thickness Skin Graft or FTSG. <br>
3. Composite skin graft.<br>
</p>

<p>The STSG process involves shaving off a thin layer of skin (of the thickness of a tissue paper) from an area of the body that heals fast, such as thigh, buttocks, or calf. The donor area is usually healed within 2-3 weeks and remained pink before getting back to its normal state with a hardly noticeable scar mark.
</p>

<p>
The FTSG process is slightly more complicated because this involves removal of the full thickness of skin (all layers) and surgically sutured to the wound. The donor site is closed directly. FTSG provides better color match and less contraction of the graft than STSG and also presents an optimal cosmetic match for facial wounds. 

</p>
<p>However, picking up new blood vessels can be more difficult in FTSG because full thickness of skin graft is used. So the dressing should not be removed before 5-7 days. 
</p>
<p>Composite grafts use more than one type of tissue, such as skin and cartilage or other tissues. It has some special application areas such as nasal rim reconstruction.
</p>

<h2 class="blogh2">Skin graft care</h2>
<p>Postoperative care of skin graft includes cares for the graft site and also for the donor site. There will be dressings on both wounds. For care of your sites you should:
</p>
<ul>
<li>Take rest for several days.</li>
<li>Keep the dressing and surrounding area clean and free from dirt or swear.</li>
<li>Not let the dressing get wet.</li>
<li>Take medicines as directed by the doctor.</li>
<li>Use ice pack on the bandage to reduce swelling, on the advice of your doctor, keeping the site dry.</li>
<li>Not scratch the wound or pick at it. The graft site may itch as it heals. </li>
<li>Get your dressing changed by the experienced hands every 4 to 7 days. </li>
</ul>


