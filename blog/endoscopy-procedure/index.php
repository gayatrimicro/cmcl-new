                     <p>An esophagogastroduodenoscopy or EGD test is also called the upper endoscopy that helps doctors examine the upper part of the gastrointestinal tract to treat symptoms and conditions affecting esophagus, stomach, and upper intestine or duodenum.</p>



<!-- <h2 class="blogh2">
 What is Colonoscope and how is it used?
</h2> -->
<p>
  An EGD test can help identify many different diseases. Here is a list of them - </p>
 <p> 
1. GERD or Barrett’s esophagus<br>
<a href="../gastrointestinal-diseases/" target="_blank"> Gastroesophageal reflux disease or GERD,</a> that happens when gastric acid flows back from the stomach into the esophagus. GERD causes damage of the esophagus wall. It can also result in the growth of abnormal cells, also known as Barrett’s esophagus that may cause cancer. 
</p>
<p>
Doctor usually advise an EGD test if you experience symptoms of GERD such as  heartburn, indigestion, regurgitation, and nausea for long time.

</p>



<!-- <h2 class="blogh2">
 Preparation before Colonoscopy:

</h2> -->
<p>
   2. Inflammation and ulcers<br>
<p>Inflammation of various parts of the upper GI tract that include -<br>
  <ul>
<li>Duodenitis - inflammation of duodenum, </li>
<li>Esophagitis - inflammation of esophagus, and </li>
<li>Gastritis - inflammation of stomach. </li>
</ul><li>
  <p>
Helicobacter Pylori infection is the main cause of inflammation which can lead to erosion and ulcer conditions such as duodenal and peptic ulcer.  
Typical symptoms of the upper GI tract inflammations are frequent nausea, vomiting, belching, bloating, and burning stomach pain. </p>
<p>
3. Cancer and Tumors<br>
 Ulceration, abnormal bumps, and polyps are the likely causes of cancer in the upper GI tract. Biopsies are taken by the doctors and sent for examination to find if they are benign or malignant. Stomach cancer and esophagus cancer are the most common types of cancer in the upper GI tract, whereas the duodenal cancer is relatively rare.</p>
 <p>
A family history of cancer or a long-term gastritis can be the risk factors for the upper GI tract cancer. Nausea, vomiting, anemia, and pain during swallowing are some of the early symptoms that may lead to upper GI tract cancer. 
</p>




<p>
4. Esophageal stricture<br>
Esophagus stricture is a condition that means narrowing of the esophagus whose probable causes may be GERD or tumors. It blocks the passage of foods and liquids from going into the stomach. </p>
<p>
Typical symptoms of esophageal stricture are chest pressure, difficulty in deep breathing, difficulty in swallowing, and vomiting. EGD test or GI endoscopy is a proven procedure to cure esophageal stricture.</p> 

<p>
5. Esophageal varices<br>
If the veins in the walls of esophagus get enlarged to the extent of rupture and bleed, the condition is called esophageal varices. The usual symptoms of this disease are blood vomit and black, bloodied stools. The continuous loss of blood can create serious health problems, with liver disease being a major risk factor. Doctors are likely to order an  EGD test or GI endoscopy to treat esophagus varices. 
</p>


<h2 class="blogh2">How EGD test is performed</h2>
<p>EGD test or Endoscopy is a painless process. Your doctor will insert the endoscope, a long, thin, flexible pipe fitted with a tiny camera and light at its leading tip, into your mouth and push it gently through your throat. You may be asked to swallow as the scope passes down your throat.</p> 
<p>
As the endoscope is forwarded down your esophagus, the images from the camera will be shown on a monitor. Your doctor will observe the image minutely to find out if there is any abnormality in your upper digestive tract. Gentle air pressure may be used to inflate your digestive tract. This allows the endoscope to move freely inside the tract.</p>

<p>
If any polyps are found they are removed by the doctor using a special surgical tool passed through the endoscope, and then sent for malignancy test. </p>
<p>
Once the doctor has finished the test, the endoscope is carefully retracted through your mouth. The entire process typically takes around 30 minutes, depending on your situation. The endoscope doesn't interfere with your breathing. 
 

</p>

