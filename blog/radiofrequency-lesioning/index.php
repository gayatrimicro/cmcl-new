                      <h1 class="blogh1">Radiofrequency lesioning: Popular method of pain management</h1>

                     
                    
                     <p>Radiofrequency lesioning (also known as radiofrequency nerve ablation) is a procedure that uses radio waves to treat pains. This is fast becoming a popular method of pain management which is safe, effective, and fairly long-lasting. </p>
<p>This provides a good treatment option for certain types of pains that are called facet joint pains. Some of the examples of facet joint pains are low back pain, neck pain, thoracic spine pain etc. </p>

<p>A quick glance at how we feel pain will help to understand the procedure well. 
All our bodily functions and activities are controlled by the nervous system which has two parts - the <strong>central nervous system</strong> (CNS) and <strong>peripheral nervous system</strong>(PNS). PNS is a network of neurons or nerve cells that are present in each and every part of our body.</p>
<p> 
When a nerve cell comes across a sensation such as inflammation, injury, blow, etc. it sends the information to the brain. The information is sent in the form of electromechanical impulses via the nerve pathways to the central nervous system(CNS). 
</p>
<p>
The CNS has two parts - the spinal cord and brain. The peripheral nerves join the spinal cord through the facet joints. The facet joints, located between the vertebrae in the spine, act as junction points where the peripheral nerves meet the spinal cord to reach the brain. On reaching the brain the impulses are interpreted as pain. 

</p>





<h2 class="blogh2">
Radiofrequency lesioning procedure:</h2>
<p>
 The radiofrequency lesioning procedure involves identifying the right nerve that is conducting the pain signal to the brain and blocking or damaging it so that the information can’t reach the brain. X-rays are used to identify the right facet joint and the nerve. </p>
<p>Once the target spot is found a probe is inserted through a needle to apply extremely low voltage for accurate placement confirmation. After it is confirmed, a small amount of local anesthetic is injected before applying a higher radiofrequency voltage so that the nerve heats up to disrupt the conductivity.</p>


</p>

<h2 class="blogh2">Encouraging facts about radiofrequency lesioning:
</h2>
<p>
  <ol class="num1">
<li>High success rate</li>
<li>Pain relief, for 3 to 18 months </li>
<li><a href="https://cmclancaster.com/blog/laparoscopy-minimally-invasive-surgery/" target="">Minimally invasive</a></li>
<li>Done under local anesthesia </li>
<li>Quick recovery</li>
<li>Minimal or no blood loss</li>
<li>Same day outpatient  </li>
</ol>
</p>


<h2 class="blogh2">Lumbar radiofrequency lesioning </h2>
<p>Lumbar refers to the lower back part of our spine which is made up of lumbar facet joints. <a href="https://cmclancaster.com/blog/lumbar-spine-surgery/" target="_blank">Lumbar pain or lower back pain</a> can result from injured facet joints in that area (cartilage, capsule, or the ligaments surrounding the joint). Pain signals from the facet joints travel via the sensory neurons to the spinal cord on their way to the brain. </p>
<p>
Lumbar facet joint pain is difficult to be detected by X-rays or MRI. At  CMC Lancaster our specialists perform a clinical exam for diagnosis and may also recommend temporary blocking of the medial branch nerve to identify the source of pain. After the pain creating nerve is identified the RF lesioning procedure is performed. </p>
<p>
Our specialist will explain to you beforehand the benefits and risks of the procedure and answer all your questions regarding this. 
</p>
<p>
Generally, radiofrequency lesioning is a safe procedure. You will likely return to work the next day. Some muscle soreness near the injection site might occur for nearly a week. Other risks, although very rare, include infection, bleeding, and nerve damage. 
</p>

