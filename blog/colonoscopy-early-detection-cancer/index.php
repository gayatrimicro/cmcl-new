                      <h1 class="blogh1">Colonoscopy - an overview</h1>
                    
                     <p>Early detection of cancer can save a life. Colonoscopy detects cancer of the colon and rectum most accurately. If you or a loved one is having abnormalities such as persistent blood in stool, weight loss or extreme tiredness for no obvious reasons, changed bowel habits, frequent stomach pain or cramps, etc. your doctor will most likely advise colonoscopy because this provides the best evaluation if your colon would have got cancer.  </p> 
<p>
The colon or large bowel or large intestine is the last part of our <a href="../gastrointestinal-diseases/" target="_blank">gastrointestinal</a> tract that receives the unabsorbed foods from the small intestine and expels them through the process of excretion. </p>
<p>
A polyp, or a growth of tissues, in the colon (including the rectum), is common in adults and is not a serious issue unless it is an adenoma. Adenomas polyps may develop into a cancerous tumor. Colonoscopy can help spot and remove adenomas polyps to prevent colon cancer. </p>



<h2 class="blogh2">
 What is Colonoscope and how is it used?
</h2>
<p>
  The colonoscope is a long, thin, and flexible tube mounted with a digital camera and light source at the end. The device is gently pushed through the anus slowly until it reaches the other end of the colon. As it moves through the tubular path of the colon, the colonoscope keeps sending the video of the path to help specialists detect abnormalities. </p>
  <p> 
If colon polyps are found during a colonoscopy they are removed then and there using a process called polypectomy. The removed polyp tissues they are then sent for testing to determine if they are cancerous. Although most polyps are benign, some may turn out to be containing cancerous cells. This shows how important is the removal of polyps in order to prevent colon cancer. 
</p>

<h2 class="blogh2">
 Preparation before Colonoscopy:

</h2>
<p>
 For smooth passing of the tube and good video image of the interior, the colon must be fully empty of waste material. Although it will depend upon your doctor’s advice usually you need to be on a low residue diet( semi-solid or liquid ) for two days before the day of the test. Additionally, oral laxatives can be used to further cleansing of the colon to make colonoscopy successful. Aspirins or drugs containing aspirin or iron tablets should be avoided one week prior to the test. Regarding other medicines including your daily doses follow your doctor’s instruction.</p>
  <p>
On the day of the test, you must come with somebody who will escort you to return home because the test requires you to be anesthetized which will take you several hours after the test to get back to normal.

</p>
<h2 class="blogh2">
Colonoscopy procedure:
</h2>

<p>
Colonoscopy is usually a painless process. You will be made to lie on your left-hand side with your legs tucked up to the chest. The colonoscope is inserted gently through the anus and forwarded up through the colon. This is preceded by air or carbon-dioxide Insufflation to inflate the colon for easier movement of the colonoscope. </p> 

<p>
The colonoscope will continue moving forward until it reaches the point where the colon meets the small intestine and then it is withdrawn slowly. During the up and down movements of the colonoscope, the doctors minutely observe the lining of the colon in the images sent by the camera.  </p>

<p>
If polyps are found during the process the doctors obtain biopsies by using polypectomy and send for analysis. Bleeding is likely during the removal of polyps which can be stopped using clips or other methods. Colonoscopy is a very safe and secure process with hardly any pain involved. You’ll be asleep during the whole procedure and return to your normal routine soon after the process.</p>


<h3 class="blogh2">After colonoscopy:</h3>
<p>Colonoscopy procedure takes around 30 minutes. You may feel uncomfortable for sometimes due to bloating, gas, or mild cramp because of insufflation process. You can go back to your usual diet once the procedure has been completed. If a polyp is found and removed from your colon, you may be instructed to be on a limited diet before going back to your usual diet.</p>

<p>
Although, the greatest contribution of colonoscopy has been in the control of colon cancer by removing the colon polyps, there are other applications of colonoscopy to diagnose other problems of large intestine such as rectal bleeding or change in bowel habit, or inflammatory bowel disease.  

</p>

