If the bulges occur in the chest cavity they are called thoracic aortic aneurysms( TAA ).
If the bulges occur in the abdomen they are called abdominal aortic aneurysms( AAA ). 
A person may as well have both types - AAA & TAA. 

</p>
                    


<h2 class="blogh2">
 Causes for aortic aneurysms
</h2>
<p>
 The reasons for the development of bulges in the artery may be due to localized erosion of the artery wall. The aortic aneurysms drain blood away from other organs that may lead to heart attack, stroke, and severe kidney damage.</p>
 <p> 
Although, it is not very clear as to why that happens the two main risk factors are being male and aging (happens mostly after 65 years). More than 1 in 50 deaths of males in this age group are caused due to aortic aneurysms. You should take a screening if you match these criteria. </p>
<p>
The other factors include smoking, high blood pressure, and high cholesterol level. It can also run in the family. </p>
<p>
Not that, all aortic aneurysms will burst readily. You may have it for years together without any palpable symptoms. That is why the detections of aortic aneurysms are mostly accidental while diagnosing other diseases. </p>

<p>

But the rupture can happen any time, causing a major medical emergency. 
</p>




<h2 class="blogh2">Symptoms of aortic aneurysms</h2>

<p>There are no specific symptoms of an aortic aneurysm until it becomes big. The symptoms from the larger aneurysms might include:

<ul>
<li>Problems with breathing or swallowing.</li>
<li>Shortness of breath.</li>
<li>Chest pain or back pain.</li>
<li>Hoarseness and coughing.</li>
<li>Nausea and vomiting.</li>
<li>Bruising on the side of the body.</li>
<li>Perspiration.</li>
<li>Weakness and loss of consciousness. </li>

</ul>
 </p>

 <p>
   In the event of a ruptured aneurysm, there will be massive internal bleeding with sudden and severe pain. 8 out of 10 people with ruptured aortic aneurysms either die before they are taken to the hospital or canâ€™t survive the surgery. 

 </p>

<h2 class="blogh2">Diagnosing aortic aneurysms</h2>
<p>Since aortic aneurysms do not put out any symptom until it is big, screening tests or examination for other conditions are normally the ways of diagnosing them.</p> 
<p>
The screening test involves an ultrasound scan that allows detection and measurement of your aorta aneurysms. Visit CMC Lancaster for an ultrasound scan that will take around 10 -15 minutes. Abdominal aorta aneurysms are most common in men aged over 65 years.</p>

