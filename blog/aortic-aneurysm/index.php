                      <h1 class="blogh1">Timely screening can save you from the danger of aortic aneurysm rupture </h1>
                    
              <p>
An aortic aneurysm is a bulge in the aorta that grows surreptitiously before bursting out to cause sudden death. About 90% of people who experienced ruptured aortic die from it. The high risk of fatality puts it in the same group as other deadly medical conditions such as cardiac arrest, stroke, or pulmonary embolism. </p>

<p>
The aorta is the main artery in the human body that runs through the chest and abdomen channeling the oxygenated blood through its many branches. Although it is wide and sturdy and designed to handle a large volume of blood, there can be development of balloon-like bulges at any part of it which are called aneurysms of the aorta. </p> 
<p> 
The bulges can be tubular(called fusiform) or round shaped (called succular). They can occur anywhere in its main stems. </p>

