                      <h1 class="blogh1">Urinary Tract Infections - treat them early before they get complicated.
</h1>
                    
                     <p>Our urinary system or tract consists of four organs - kidneys, ureters, bladder, and the urethra. A UTI or Urinary tract infection can occur in any of these parts. However, most infections affect the bladder and urethra, the parts belong to our. Personal hygiene plays a very important role in keeping UTI at bay, especially for women.</p>
<p>A lower UTI is easier to treat than the upper UTI that includes the kidney infection which gets complicated more often. </p>

<p>
Urinary tract infections are bacterial infections that can affect you irrespective of your sex or age. However, statistics show that women are at greater risk of UTI than men. While women of any age can develop a UTI, men usually become susceptible to it after the age of 50.  </p> 




<h2 class="blogh2">
 What may cause a UTI?

</h2>
<p>
  Infection of the bladder(cystitis) and the infection of the urethra(urethritis) attribute to UTIs in most cases, unless it has gone up to infect the kidneys. The risk factors include:

<ul>
<li>Frequent and intense sexual intercourse, especially with multiple or new partners</li>
<li>Diabetes</li>
<li>Poor personal hygiene</li>
<li>The bladder remains unemptied after urinating </li>
<li>Having a urinary catheter</li>
<li>Bowel incontinence</li>
<li>Blocked flow of urine</li>
<li>Kidney stones</li>
<li>Some forms of contraception</li>
<li>Pregnancy</li>
<li>Menopause</li>
<li>Procedures involving the urinary tract</li>
<li>Suppressed immune system</li>
<li>Immobility for a long period</li>
<li>Use of spermicides and tampons</li>
</ul>

</p>


<h2 class="blogh2">Symptoms of UTI
</h2>
<p>You may notice one or more of the following symptoms if you have got a UTI, but some of them may come due to other conditions as well and disappear after a day or two. 
  <ul>
<li>A strong, persistent urge to urinate</li>
<li>A burning sensation when urinating</li>
<li>Passing frequent, small amounts of urine
<li>Urine that appears cloudy</li>
<li>Urine that appears red, bright pink or cola-colored — a sign of blood in the urine</li>
<li>Strong-smelling urine</li>
<li>Pelvic pain, in women — especially in the center of the pelvis and around the area of the pubic bone</li>

  </ul>
</p>


<h2 class="blogh2">
Bladder infection </h2>
<p>
 Bladder infection is caused when bacteria that enter through the urethra and get into the bladder, are not flushed out by the flow of urine. The bacteria cling to the bladder wall and multiply. This overpowers the body’s immune system and affects the efficacy of the bladder. </p>
<p>Women are more likely to catch bacteria as their urethra is located very close to the vagina and rectum which are the main sources of bacteria.</p>

<p>
Men can have the problem of enlarged prostate at advanced age which can choke the flow of urine to cause a bladder infection.
</p>
<p>
Bladder infections are usually acute in nature, meaning they occur suddenly. But there can be chronic cases of bladder infection as well that can recur over a long time. Early diagnosis and treatment can eliminate bladder infection and also prevent the patient from a greater risk of kidney infection. 

</p>

<h2 class="blogh2">Kidney infection
</h2>

<p>
  Kidney infections are caused mainly when the bacteria in the bladder spread to the kidneys. A type of bacteria called E. coli is mainly responsible for kidney infections. However, a kidney infection can also take place due to a bacterial invasion through the skin to mix with blood and reach the kidney. Also, you can get a kidney infection after a <a href="https://cmclancaster.com/blog/kidney-dialysis-treatment-for-kidney-failure/" target="_blank"> kidney surgery</a>, but that is very uncommon. 

</p>


<h2 class="blogh2">
 Treatments for UTI

</h2>


<p>Since bacteria are the main causes of UTI they are normally treated with prescription medications in the form of antibiotics or antimicrobials to kill the bacteria and medications for relief from pain and burning. </p>
<p>
However, the type of medication depends upon factors such as the symptoms and the medical history of the patient. For relieving pain and burning sensation associated with a bladder infection, doctors generally prescribe phenazopyridine, also known as pyridine. </p>
<p>
Viruses and fungi can also cause UTIs that need antiviral and antifungal medications for treatment.
</p>

