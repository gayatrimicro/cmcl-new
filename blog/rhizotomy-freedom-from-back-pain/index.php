                      <h1 class="blogh1">Rhizotomy can give you freedom from pains</h1>

                     
                    
<p>Do you fear that debilitating back pain of yours can start any day again to make movements so much difficult and yet have no clue as to how you should deal with that because it has so far defied all your efforts of pain relief? </p>
<p>
Then you may be the right candidate for ‘rhizotomy’ that can bring you complete pain relief for a long time.  The procedure is minimally invasive and takes around 20-30 minutes at an outpatient setup.</p>
<p>
Rhizotomy, or better known as facet rhizotomy, provides pain relief by blocking the pain impulses before they can reach the brain. Most patients have experienced pain reliefs through facet rhizotomy for months or even years.</p>
<p> 
Rhizotomy is ideal for the pain conditions that are caused by the inflammation or degeneration of the facet joints. Different types of such pains are:
</p>
<p>
  <ul>
<li>Chronic back pains- cervical, thoracic, or lumbar types.</li>
<li>Muscle spasms.</li>
<li>Pain if you have to lean backward (but no problem when you lean forward).</li>
<li>Sitting for a long time aggravates your lower back pain, long driving may become nearly intolerable. </li>
<li>Shooting or burning pain that spreads over the shoulder and upper back, but not extends to arms.</li>
<li>Depending on the location of the problematic facet joints pains can be at ribs, chest, abdomen, buttocks, groin, legs, neck, shoulder. Even headaches can be a facet joint problem.</li>

  </ul>
</p>





<h2 class="blogh2">
Rhizotomy procedures
</h2>
<p>
They are <a href="https://cmclancaster.com/blog/laparoscopy-minimally-invasive-surgery/" target="_blank"> minimally invasive surgical procedures</a> that are almost painless and are performed under local anesthesia to numb the procedure site while making you comfortable and awake but little sedated.</p>

<p>The surgeon will make a tiny incision, nearly ¼ of an inch, close to the target facet joint at your back. Next, he will insert a 7 mm tube into the incision to access the medial branch nerve. An endoscope with an HD camera and a torch attached at the front end will be inserted then into the metal tube which gives the surgeon a clear view of the medial branch nerve. Using microscopic instruments the surgeon will sever the medial branch nerve. </p>
<p> 
The metal tube is then removed back. There will be no stitch, only a small Band-Aid will be put over the incision spot. </p>
<p>
Another similar procedure is Radiofrequency Rhizotomy, also known as <a href="https://cmclancaster.com/blog/radiofrequency-lesioning/" target="_blank">Radiofrequency Lesioning</a> / Radiofrequency nerve ablation(RFNA), that involves using radio waves to damage the pain-conducting nerves. The pain sensation can stop for a period of time until the nerve is regenerated. Meanwhile, the degenerated facet joint can be improved through physiotherapy or some other treatments.</p>
<p>
Both the endoscopic rhizotomy and radiofrequency rhizotomy procedures are conducted at the CMC Lancaster on an outpatient basis. Our spine surgeons will advise you which one should be best for your condition. </p>
<p>
You’ll be released within an hour after the procedure is complete. You will be advised to bring someone to be with you till you reach home. </p>
<p>
You may be able to go back to work after a couple of days but you’ll have to be careful, not to carry any heavy load during recovery. Full recovery will take a few weeks time, during when you’ll be required to come in to go through physical therapy to recover and gain back full strength and mobility as fast as possible. 

</p>

