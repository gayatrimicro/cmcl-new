<h3 class="blogh3">Peritoneal dialysis:</h3>
<p> In this process the blood is filtered inside the body using a special cleansing fluid, put into your abdomen through a catheter. The waste from the blood that passes through the small blood vessels in the abdominal cavity(also called the peritoneal cavity) are absorbed by the fluid, before it is drained out of the body. 
Peritoneal dialysis can be of different kinds, but the two methods that are used more often than not are:  </p>
<p>
  <ul>
<li>Continuous Ambulatory Peritoneal Dialysis (CAPD) - Performed without using any machine.</li>
<li>Automated Peritoneal Dialysis (APD) - Performed using a special machine called a cycler.</li>
</ul>
</p>



<h2 class="blogh2">When is dialysis required?</h2>
<p>Your doctors will test your kidney performances to make sure if dialysis would be necessary. Dialysis is generally opted for if it’s an end stage kidney failure - when hardly 10-15% of your kidney functions are happening with a GFR level below 15 ( GFR for normal people range from 90 -120 ). </p>
<p>
Your lab tests show increasing toxic level in your blood that forces you to display symptoms such as:</p>
<p>
  <ul>
<li>Lethargy.</li>
<li>Weakness.</li>
<li>Shortness of breath.</li>
<li>Generalized swelling (edema)</li>
<li>Generalized weakness due to anemia.</li>
<li>Loss of appetite.</li>
</ul>
</p>
<p>
During the dialysis treatment usually there is no pain but a little discomfort when the needles are put into the blood vessels or fistula. However, for some patients there can be a drop in blood pressure with some minor side effects such as stomach sickness, vomiting, headache, or cramps. With frequent treatment, these problems disappear.  </p>
<p>
Life expectancy on dialysis varies from person to person, depending on your other medical conditions and regularity in your treatment plan. Average life expectancy on dialysis is 5-10 years, however, many patients are known to have lived well on dialysis for 20 or even 30 years.</p>

