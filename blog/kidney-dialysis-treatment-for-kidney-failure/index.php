                      <h1 class="blogh1">Kidney Dialysis: A Major Treatment Options for Kidney Failure</h1>
                    
                     <p>Ever since it was introduced, dialysis has been considered a major treatment option for kidney failures. Whether it’s acute kidney failure or a chronic kidney failure, when other options have been tried and failed, the doctors can still save lives using dialysis and kidney transplantation. </p>
<p>
While kidney transplantation is a complicated surgery and depends upon the availability of a healthy replacement kidney, the dialysis offers a much easier procedure that replicates and bypasses the defective kidney(s). </p>
<p>
The Dialysis can be your long-term treatment strategy or it may be used for your survival before you undergo kidney transplantation. There are some dialysis procedures that can be performed at home either with the help of a nurse or independently by the patient. </p> 
<p>
Nature has given us two kidneys but we can also survive with one good kidney. A lot of people are born with one kidney; many lose a kidney due to disease or accident and live on. However, for some reasons, if that kidney also develops some problem, it poses great threats to our lives.
</p>

<h2 class="blogh2">
 What is dialysis?
</h2>
<p>
 <b> Dialysis is a treatment procedure that replicates our kidney functions artificially.</b> If we are diagnosed with severe kidney failure, dialysis can save our lives by keeping our body in balance by: </p>
  <p>
    <ul>
<li>Removing waste, salt and extra water from the blood,</li>
<li>Keeping a safe level of certain chemicals in our blood, such as potassium, sodium and bicarbonate, and </li>
<li>Helping to control blood pressure.</li>
</ul>

</p>
<h2 class="blogh2">There are two types of dialysis:</h2>

<h3 class="blogh3">Hemodialysis:</h3><p> This is the most commonly used type of dialysis that uses an artificial kidney(hemodializer) placed outside the body to remove waste and extra fluid from the blood. The blood is taken out of the body and passed through the hemodializer for cleaning and filtering. The filtered blood is then returned into the body.</p>
<p> 
Hemodialysis is usually done three times a week for a duration of about three to four hours or longer each session. </p>
<p>
A minor surgery is done usually on your arm or leg to get access into the blood vessels. Alternatively, access to blood flow is made by joining an artery to a vein under the skin of an arm to make a bigger blood vessel called a <a href="https://inflammatoryboweldisease.net/symptoms/complications/fistula/" target="_blank" rel="nofollow">fistula</a>.</p> 





