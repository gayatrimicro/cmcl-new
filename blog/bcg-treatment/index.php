                      <h1 class="blogh1">Bacillus Calmette-Guerin (BCG) For Bladder Cancer</h1>
                    
                     <p>Danger from cancer doesn’t stop after your treatment is over, because it may come back after a gap of time. In fact, doctors are wary of using the word ‘cure’ in the treatment of cancer because your body could still have some undetected cancer cells that might come to the fore after some time.   </p> 
<p>
When it comes to <a href="https://cmclancaster.com/blog/bladder-cancer-survival-rates" target="_blank">bladder cancer</a>, high grade non-invasive papillary (Ta) tumors are more likely to come back after treatment. High-grade tumors, even if they don’t invade the muscle layer and spread to other parts of the body, are significant in terms of bladder cancer mortality. Tumor recurrence is a major concern even for patients treated for low-grade tumors.  </p>
<p>
The risk of cancer recurrence is maximum within the first five years after the finish of treatments and is reduced with the passing of more years. However, it varies from person to person and is also dependent on multiple factors such as the type and stage of cancer, genetic factors, and type of treatment to decide if bladder cancer might come back.
</p>



<h2 class="blogh2">
 Immunotherapies reduce recurrence of bladder cancer.
</h2>
<p>
  Treatment for in-situ non-invasive bladder cancer involves surgical removal of the tumor followed by one dose of chemotherapy within the bladder. But things don’t end there. Patients with moderate to high-grade disease are advised to undergo BCG treatment.  </p> 
<p>
BCG (Bacillus Calmette-Guerin) is the first FDA approved intravesical immunotherapy. A course of low doses of live BCG bacteria helps reduce the risk of recurrence of bladder cancer by stimulating an immune response that fights the bacteria as well as any nearby bladder cancer cells. </p>
<p>
BCG is given directly into the bladder using a catheter. Following criteria are important to make the therapy effective</p>
<p>
<ul>
<li>The patient should be immunocompetent</li>
<li>The tumor burden is small</li>
<li>BCG makes direct contact with the tumor</li>
<li>The dose is adequate to incite a reaction</li>
</ul>
</p>
<p>
Statistics show that 70% of patients with CIS have got their cancer eradicated through BCG vaccination after the main treatment is done. 
</p>

<p>
BCG is usually given once a week for six weeks and then there will be a gap of six weeks. If the result is hopeful the treatment may be continued. The procedure takes around three hours in an outpatient facility, after which the patient can go home.</p>
<p> 
Precautions must be taken after the procedure as BCG is a live vaccine. The patient will be told about the precautions by the nurse or doctor.</p>
<p>
Some side effects are likely from the BCG treatment such as frequent urinating, or pain or blood with urine. Always inform your doctor if the symptoms don’t improve or if you get other symptoms such as fever, joint pain, or a cough.



</p>

