                      <h1 class="blogh1">Early detection of Carotid artery disease helps prevent strokes</h1>

                     
                    
<p>Risk of stroke is increasingly defying age barriers. Earlier what used to be limited to people of 70 years and above, is now occurring to people of below 40 years, with its early symptoms often getting unrecognized or ignored. 
Carotid artery disease or carotid stenosis is the root cause of stroke which leaves the patient dead or critically disabled with paralysis.</p>
<p>
A major stroke usually comes with a trail of mini-strokes or TIAs (Transient Ischemic Attacks) which are initial stages of carotid artery disease. The symptoms of carotid artery disease are one or more of the following strange sensations that generally last for a few minutes: </p>
<p>
  <ul>
<li>Sudden weakness, numbness, or paralysis in the face, arm, or leg, typically on one side of the body. </li>
<li>Sudden slurring of speech and trouble in understanding speech, confusion.</li>
<li>Sudden vision trouble in one or both eyes.</li>
<li>Sudden dizziness. </li>
<li>Difficulties in walking.</li>
<li>Sudden start of a headache. </li>
</ul>
</p>






<h2 class="blogh2">
How Carotid artery disease occurs
</h2>
<p>
Carotid arteries, the type of a major blood vessel, are located around temples of the head, transporting oxygen and nutrients to the front part of the brain. This part of the brain is responsible for our thought process, speech, and personality in addition to our sensory and motor functions. </p>
<p>
The carotid artery disease or stenosis is basically clogging of the arteries due to plaque buildup, much the same way as the plaque formation in coronary arteries. Plaque building in the carotid arteries is called atherosclerosis which causes narrowing of the carotid arteries with plaque, made up of protein, fat, calcium, cellular waste, and some amount of cholesterol.  </p>
<p>
Atherosclerosis is a gradual process that impedes blood flow to the brain more and more over time and also makes the arteries less flexible. Lack of oxygen in the brain impacts its health by killing brain cells. </p>
<p>
Initially, it leads to TIA conditions when the plaque deposits are light and temporary which later develop into full-fledged strokes.   
</p>

<h2 class="blogh2">
Treatments for carotid artery disease
</h2>

<p>
  Early detection of carotid artery disease helps prevent strokes. Never make the mistake of ignoring symptoms such as sudden temporary numbness of face, arms, and legs, or speech problem or confusion, etc. Call 911 as soon as possible. </p>
<p>
The treatment of stroke or TIA is not just healing the ongoing symptoms but also about preventing further attacks. At CMC Lancaster your diagnosis and treatment will be handled by our renowned doctors.</p>
<p>
If you are diagnosed with an early stage of carotid artery disease our doctors will stress on lifestyle changes along with medications. Eating healthy food, regular exercise, and quitting smoking are essential in the treatment of carotid stenosis. 
</p>
<p>
If you are diagnosed with an advanced stage of carotid artery disease, it may be necessary to surgically remove the blockage. There are two procedures to do that - carotid endarterectomy and a carotid artery stent.</p>
<p> 

The <strong>carotid endarterectomy</strong> involves removing blockages by making an incision in the carotid artery through the front of your neck. Your blockages can be cleared entirely through this process which will ensure a long lasting effect on preventing strokes. The artery will be stitched neatly at the end. </p>
<p> 

A <strong>carotid artery stent</strong> provides a method for cleaning the artery when the plaques are inconveniently located. Also known as stenting, this process comes in handy if your blockage is large or you have other serious health problems that make your surgery a risky affair.  
</p>


