                      <h1 class="blogh1">Gallbladder Removal Surgery (Cholecystectomy)</h1>
                    
                     <p>We don’t know! Maybe you or I and a million others are moving with stones in our gallbladder, and we simply feel no difference. Formation of gallbladder stones is not a big issue unless they block the ducts including the common bile duct and pancreatic duct, preventing the release of bile from the gallbladder. If that happens, it is terribly painful.   
</p>
<p>
The gallbladder is a small pouch-like organ placed just below the liver in our body and stores the bile produced by the liver and help the flow of biles reach the small intestine to aid digestion.  </p>
<p>
The exact reason of why stones are formed in the gallbladder is still unknown but it is more or less established that the bile in some people contains excess cholesterol or bilirubin, and that may get hardened due to lack of water, leading to the formation of stones. Gallstones may also develop if bile is not removed from the gallbladder completely. 
 </p>
<p> 
While most gallstones are made of cholesterol, their sizes may vary from the size of a sand grain to as big as a golf ball. Pigment stones, composed primarily of bilirubin and calcium in the bile, can occur in people with certain conditions, such as <a href="https://liverfoundation.org/for-patients/about-the-liver/diseases-of-the-liver/cirrhosis/" target="_blank" rel="nofollow">cirrhosis</a> of the liver or blood disorders. 
                     </p>
                    
                     <h2 class="blogh2">
                       Symptoms of Gallstones
                     </h2>
                     <p>Gallstones in your body remain undiscovered in most cases and only causes symptoms if the stones move from the gallbladder into one of the ducts - the hepatic duct, cystic duct, and the common bile duct. The symptoms include: </p>
<p>
  <ul>
 1. Severe pain in the upper abdomen or back. The back pain occurs between the shoulders or under the right shoulder.  <br>
2. Increase in abdominal pain after eating a fatty meal<br>
3. Jaundice<br>
4. <a href="https://cmclancaster.com/blog/major-causes-and-symptoms-of-fever/" target="_blank">Fever</a> and pain, if the gallbladder or bile duct becomes infected.<br>
</ul>
</p>

<h2 class="blogh2">
 Risk Factors For Gallstones
</h2>
<p>
Women are twice as prone to have gallstones compared to men and that usually appears after the age of forty. It is found that women with native American and Mexican - American origins have high rates of gallstones, while women with Asian or African ancestry have fewer chances of having them.  </p>
 <p> 
Apart from the ethnic factors the other causes that increase your risk of getting cholesterol gallstones are:
 
</p>
<p>
  <ul>
<li><a href="https://cmclancaster.com/blog/bariatric-surgery/" target="_blank"> Obesity and excess weight</a> </li>
<li>Pregnancy</li>
<li>High-fat, high-cholesterol or low-fiber diet</li>
<li>A family history of gallstones</li>
<li>Diabetes</li>
<li>Liver disease</li>
<li><a href="https://cmclancaster.com/blog/crohns-disease-ibd/">Crohn's disease</a> </li>
<li>Rapid weight loss</li>
<li>Certain cholesterol-lowering medications</li>
<li>Medications that contain <a href="https://www.hormone.org/hormones-and-health/hormones/estrogen" target="_blank" rel="nofollow"> estrogen</a>, such as hormone therapy drugs and oral contraceptives</li>
</ul>
</p>


<h2 class="blogh2">
 Diagnosis of Gallstones
</h2>
<p>
  The tests that are used by the doctors to diagnose gallstones are: </p>
<p>
  <ul>
<li>General tests – such as physical examination and x-rays</li>
<li><a href="https://www.urgentcaretexas.com/blog/ultrasounds-to-detect-major-disorders/"> Ultrasound Test</a></li>
<li><a href="https://www.niddk.nih.gov/health-information/diagnostic-tests/endoscopic-retrograde-cholangiopancreatography" target="_blank" rel="nofollow"> Endoscopic retrograde cholangiopancreatography (ERCP)</a>,</li>
<li><a href="https://www.verywellhealth.com/hida-scan-overview-4171824" target="_blank" rel="nofollow"> Hepatobiliary iminodiacetic acid (HIDA) scan</a>, – a special type of nuclear scan that assesses how well the gallbladder functions</li>
<li><a href="https://www.ncbi.nlm.nih.gov/pmc/articles/PMC3292642/" target="_blank" rel="nofollow">Magnetic resonance cholangiopancreatography</a> (MRCP) – a form of the body-imaging technique magnetic resonance imaging (MRI). The person’s liver, biliary and pancreatic system is imaged using an MRI unit. The image is similar to an ERCP test.</li>
</ul>
</p>
<h2 class="blogh2">
 Gallstone Treatments
</h2>
<p>
Gallstone treatments are necessary when the symptoms (especially the severe pains) affects your quality of life. The type of treatment depends upon the size and location of the gallstones. They include:   </p>
<ul>
<li>Dietary modifications – Avoiding fatty foods and dairy products as much as possible.</li>
<li>Lithotripsy – Cracking gallstones using the sound waves. Applicable if the stones are small and soft.</li>
<li>Medications – Dissolving gallstones using medicines, but this treatment is only rarely given, due to side effects and a variable success rate</li>
<li>Surgery methods such as Laparoscopic cholecystectomy and <a href="https://www.betterhealth.vic.gov.au/health/conditionsandtreatments/laparotomy" target="_blank" rel="nofollow">laparotomy</a>.</li>
</ul>
</p>
 <h3 class="blogh3">         
Surgical Removal of Gallbladder</h2>
<p>
Surgical removal of the gallstones or the entire gallbladder is the most effective treatment that gives relief early and is performed in 80% cases. The surgery techniques to remove gallbladder include:</p>

<h3 class="blogh3">Laparoscopic Cholecystectomy</h3> 
<p> Also known as ‘keyhole surgery,’ this minimally invasive surgery is performed by making small incisions to insert advanced surgical instruments. The post-surgery healing takes minimum time which makes the patient get back to work fast. </p>
<h3 class="blogh3">Laparotomy(Open surgery)</h3>  
<p>Conventional method of gallbladder surgery by making a wider abdominal incision. Open surgery might be necessary if you have scarring from prior operations or a bleeding disorder.
</p>





