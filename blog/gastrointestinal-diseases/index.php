                     <p>Gastrointestinal tract comprises the most important functions of the body - intake of foods and drinks, digesting and metabolising them, and removing the undigested substances from the body in the form of excrete. However simple it may sound, in actuality, the functions are not so simple because of the involvements of so many components and factors. The long and complex GI tract faces challenges frequently and develops various health conditions or diseases as explained below -
</p>


<h1 class="blogh1">
 Gastroesophageal Reflux Disease (GERD)
</h1>
<p>
  This is a common condition, also known as ‘acid reflux’ occurs in our body when we feel burning pain in the middle of our chest, generally happen after meals or at night. Occasional acid reflux or heartburns are innocuous but if the symptom occurs oftenly, twice or more in every week then it could be an early sign of Gerd. </p>
  <p>
Inflammations in the oesophagus and mouth due to flowing back of stomach acid or undigested stomach contents are the main causes of Gerd.</p>
<p>
There are other symptoms too, causing variety of discomforts in our chest, throat and mouth. Here is a list of problems that might occur due to Gerd -
</p>
<p>
  <ul>
<li>Persistent heartburn,</li>
<li>Sour or bitter taste in mouth, due to regurgitated stomach contents,</li>
<li>Sore throat,</li>
<li>Coughing, feeling like having a lump in the throat,</li>
<li>Voice turning hoarse,</li>
<li>Difficulty in swallowing,</li>
<li>Eroding teeth.</li>

  </ul>
</p>
<p>
  Generally people tend to suppress or eliminate these symptoms by taking antacids or by avoiding foods or beverages, but persistent Gerd conditions may require prescription medication or even surgery.

</p>


<h2 class="blogh2">
 Gallstones

</h2>
<p>
  Our gastrointestinal tract consists of a small, pear shaped sack called ‘gallbladder’ which plays a vital role in the GI function because it stores and secretes bile to aid digestion. Gallstones are hard deposits that are formed in the gallbladder due to -</p>
  <p><ul>
<li>hardening of the residual bile if the gallbladder is not emptied properly, </li>
<li>too much cholesterol or waste in the bile.</li></ul> </p>
<p>
Gallstones block the canal through which the bile is flowed into the intestine. This can lead to excruciating pain in the upper right abdomen. The treatments of gallstones involves dissolving the gallstones either through medicines or by surgery. Even removing the gallbladder may be an option in case the stones are not dissolvable.
</p>

<h2 class="blogh2">
Celiac Disease
</h2>

<p>
Celiac disease is an autoimmune response developed as reaction to the consumption of gluten. Gluten is a protein found in wheat, rye, and barley. Eating gluten rich foods triggers immune system attack and damage a tiny finger like protrusion in the small intestine called ‘Villi’ which is responsible for absorption of nutrients from food into blood. </p>
<p>
Symptoms of celiac disease includes -<br>
<ul>
<li>Weight loss, bone loss</li>
<li>Slow growth for children</li>
<li>Chronic diarrhea, often bloody</li>
<li>Constipation or diarrhea. </li>
<li>Vomiting.</li>
<li>bloating and abdominal pain.</li>
<li>Irritability.</li>
<li>Anemia, fatigue mainly in adults  </li>
<li>Depression, seizure mainly in adults</li>

</ul>
</p>
<p>Majority of the people with celiac disease aren’t even aware of the disease in them, and  strangely some people may not have any of the symptoms. 
There is no treatment of celiac disease except food control. Completely avoiding the gluten rich foods only help to avert celiac disease. Alternative foods include brown rice, quinoa, lentils, soy flour. amaranth, and corn flour. 
</p>


<h3 class="blogh2">Crohn’s disease and Ulcerative colitis</h3>
<p>These diseases belong to the family of digestive conditions called Inflammatory Bowel Disease or IBD which is caused due to abnormal reactions of immune system.</p>
<p><a href="../crohns-disease-ibd/" target="_blank">Crohn’s</a>  can affect any part of the GI tract but mostly it affects the terminal ileum, which is the link between the end of small intestine and the start of large intestine (also known as colon). Most common Crohn’s symptoms are abdominal pain, diarrhea, rectal bleeding, weight loss, and fever. </p>

<p>Ulcerative colitis affects only the large intestine or colon, damaging the lining of the colon. Symptoms of ulcerative colitis include diarrhea with pain, blood in stool, abdominal cramps, and urgent bowel movements.</p>
<p> 
Treatments of Crohn’s and Ulcerative colitis involves medications or surgery. Medications include -<br>
<ul>
<li>Anti-inflammatory drugs</li>
<li>Immune system suppressors</li>
<li>Antibiotics</li>
<li>Pain relievers</li>
<li>Iron supplements.</li>
</ul>
</p>


<h3 class="blogh2">IBS:</h3>
<p>Irritable Bowel Syndrome or IBS is a common digestive condition which affects several millions of people in the United states. Unlike IBD which is an autoimmune problem that damages the lining of the GI tract, IBS is a gastrointestinal functional disorder which doesn’t damage the GI tract. </p>

<p>There are two types of IBS based on their end effects - IBS-D and IBS-C. Patients with IBS-D experience stomach pain accompanied by diarrhea while those with IBS-C suffer from stomach pain, bloating and constipation. Some people with IBS may have both symptoms such as hard and dry stool on one day and loose motion on the other day.</p>
<p>
While these symptoms can happen in normal day to day life IBS is diagnosed in a person if the symptoms happen 3-4 times a month for several months. </p>
<p>
The causes of IBS is not yet known and there is no cure of IBS also. Treatments of IBS is intended to reduce the symptoms and improve the quality of life. Treatments generally involve dietary control i.e. identifying and avoiding foods that trigger IBS symptoms in a person.  Consuming low-fat and high-fibrous diet and avoiding dairy products, alcohol, caffeine, artificial sweeteners, etc. are also important to maintain longer period of no IBS symptoms. Yogurt which consists of probiotic bacteria is helpful in reducing IBS conditions.</p>
<p> 
Stress can cause IBS symptoms, therefore destressing procedures such as cognitive - behavioral therapy or low-dose antidepressants are also sometimes included in the treatments.

</p>

