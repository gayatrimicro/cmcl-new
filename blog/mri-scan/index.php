<p>
A <strong>closed MRI machine</strong> is a large tube (with both ends open) inside which the patient has to lie on a table. The images produced are generally of high quality but it is important that the patient lies on the table very still. </p>
<p>
An <strong>open MRI machine</strong> has everything the same as the closed type machine except that there is no tube. Instead, there is more open space. The open MRI machine is developed to allow more comfort to patients who are claustrophobic or obese. However, the image quality is slightly compromised. </p>



<p>
In a <strong>standing or sitting type MRI machine</strong> provides the patient with the comfort of sitting or standing during the scan. However, the image quality may not be of very good.</p>

<h2 class="blogh2">
  MRI scan for brain and spinal cord
</h2>
<p>
  Brain and spinal cord are the two areas where MRI is used most frequently. MRI is superior to other imaging methods such as CT scan when it comes to detection of damages in the brain or spinal cord. The below conditions are generally diagnosed through MRI: 
  <ul>
<li>Multiple sclerosis.</li> 
<li>Disorders of the eye and inner ear.</li>
<li>Brain Injuries from trauma.</li>
<li>Aneurysms of cerebral vessels.</li>
<li>Spinal cord injuries.</li>
<li>Stroke.</li>
<li>Tumors.</li>

  </ul>
 </p>
 


<h2 class="blogh2">MRI scan for heart and blood vessel abnormalities</h2>
<p>
  The following vital information about your heart and blood vessels are found through the MRI scan. 

  <ul>
<li>Current size and function of the heart’s chambers.</li>
<li>The heart wall thickness and its movements.</li>
<li>The amount of damage caused due to a heart attack or heart disease.</li>
<li>The extent of inflammation or blockages in the blood vessels.</li>
<li>Development of the conditions such as aortic aneurysms and dissections. </li> 

  </ul>

</p>

<h2 class="blogh2">
  Things that should be known to the patient beforehand
</h2>
<ul style="list-style-type: decimal !important;">
<li>Depending on the condition an MRI test may last from 15 minutes to over an hour during when the patient shouldn’t make any movement in order to prevent blurring of an image. </li>
<li>During the scan, there may be repetitive tapping sounds from the internal parts of the machine. That is normal. The patient will be provided with earplugs or music to help block the noise. </li>

<li>During the scan, the patient may be asked to perform some simple tasks like tapping thumb against fingers or answering some questions to see which part of the brain controls them.</li>

</ul>



