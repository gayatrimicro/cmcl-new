                      <h1 class="blogh1">All you wanted to know about MRI scan</h1>
                    
                     <p>Many people get worried if doctors ask them to take an MRI test, fearing complication in their conditions. But MRI scan can help to rule out suspected complications and makes way for non-surgical treatments. </p>
<p>
<strong>Magnetic resonance imaging</strong>, the full form of <strong> MRI</strong>, is an imaging process that uses magnetic properties. This is an absolutely safe and painless(non-invasive) procedure that makes use of a large magnet, radio waves, and a computer to produce clear and detailed images of your body parts to check for a tumor or other abnormalities. 
</p>

<p>There can be full body imaging or imaging of the individual parts. The list includes: 
<ul>
<li>Brain and spinal cord</li>
<li>Bones and joints</li>
<li>Breasts</li>
<li>Heart and blood vessels</li>
<li>Internal organs - liver and bile ducts, kidneys, spleen, pancreas, uterus, ovaries, and prostate.</li>
</ul>
</p>
<p>
3D imaging can also be possible to make viewing from different angles. 


</p>

<h2 class="blogh2">
MRI test procedure and options
</h2>
<p>MRI scanning is performed by a <a href="https://www.insideradiology.com.au/radiographer-medical-imaging-technologist/" target="_blank" rel="nofollow">radiographer</a>. The machine creates a low-level magnetic field inside it which influences the atoms of the patient’s body. Atoms in different types of tissues react in different ways. These reactions are sensed by the scanner and are decoded in a computer for visual representation. The computer is hooked to the scanner but kept in another room. </p>
<p>
There are three types of MRI scan machines - traditional closed MRI, open MRI, and standing or sitting MRI.</p><br>

 
