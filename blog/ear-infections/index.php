                     <p>The hearing mechanism of our ears consists of three main sections - the outer ear, middle ear, and inner ear. Ear infections which are mainly bacterial or viral infections can affect any of these sections depending on the cause of infection. 
                     </p>
                     <h2 class="blogh2">
                       Outer ear infection (Swimmer’s ear):
                     </h2>
                     <p>An outer ear infection is sometimes referred to as swimmer’s ear. Because after swimming or a shower some water may remain in the outer ear portion. This water if not removed or cleaned properly make that area damp and an ideal breeding ground for bacteria that may reach there via the finger or any other object of scratching the outer lining of the ear.  </p>
<p>
  The symptoms of outer ear infection include -
  <ul>
  <li>Severe pain</li>
<li>Itching </li>
<li>Redness in the ear</li>
<li>Tenderness in the earlobes</li>
<li>A feeling of fullness in the ear</li>
<li>Muffled hearing</li>
<li>Ear drainage</li>

</ul>
</p>
<h2 class="blogh2">
  Treatment 
</h2>
<p>
  Swimmer’s ear may heal over some time on their own. If they don’t heal then antibiotic ear drops can be applied many times a day as prescribed by the doctors. Antibiotic drops may also be mixed with steroids to reduce swelling of the ear canal. </p>
<p>
Fungus infection is a more common cause of outer ear infection for people with diabetes or a depleted immune system. It is normally treated by doctors with antifungal ear drops. </p> 
<p>
Keeping the outer ear area dry is very important during the healing process. To reduce pain over the counter medicines like ibuprofen or acetaminophen may be used. For more severe symptoms prescription drugs may be suggested by the doctors.
</p>
<h2 class="blogh2">
  Middle ear infection:
</h2>
<p>
  This type of infections is usually originated from a cold, throat infection, or allergy attack. 
As we know that the ear has a partition wall called eardrum which vibrates in resonance to the external sound waves.</p>
<p> 
 The middle ear is located right behind the eardrum and is usually filled with air but can become filled with fluids due to cold or respiratory infections. Once the cold is cleared the fluid is drained out through the eustachian tube that connects the ear to the back of the nose. </p>
<p>
But in case the fluid is not drained but trapped in the middle ear, it will result in bacterial infection and cause the eardrum to bulge and pain along with partial hearing impairment which is termed as middle ear effusion.              
</p>
<h2 class="blogh2">
 Inner ear infection:
</h2>
<p>
This is not generally a type of infection caused by the bacterial or viral attack but is inflammation or irritation of the parts of the ear involved in the process called labyrinthitis. The symptoms of inner ear infection include:</p>
<p>
  <ul>
    
<li>Pain in the ear</li>
<li>Dizziness</li>
<li>Nausea</li>
<li>Vomiting.
</li>

</ul>
</p>
