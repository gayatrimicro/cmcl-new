                      <h1 class="blogh1">Major Causes of Urinary Incontinence</h1>

                     
                    
<p>This is one of those health conditions that you would want to discuss with your doctor only. 
If you are under the fear that there could be sudden pressure of urination anywhere and anytime that you wouldn’t be able to resist, it’ll drain your confidence for sure. </p>
<p> 
Medically, this condition is called urinary incontinence which isn’t a disease in itself but a symptom that signals some underlying problems. </p>


<h2 class="blogh2">
Causes of urinary incontinence
</h2>
<p>
<ul>
<li>Overactive bladder muscles.</li>
<li>Stress on the bladder.</li>
<li>Weak pelvic floor muscles.</li>
<li>Enlarged prostate or benign prostate hyperplasia.</li>
<li>Bladder cancer or prostate cancer.</li>
<li>Interstitial cystitis.</li>
<li>Side effect from prior surgery.</li>

</ul>
</p>

<h2 class="blogh2">
Urge incontinence

</h2>

<p>
  Our urinary tract consists of a bladder that stores urine before passing it out through the urethra. The bladder and the urethra are controlled by three sets of muscles, called the sphincter muscles, for filling, storing and removing urine. </p>
  <p>
In normal condition, a balance prevails among the muscles. Urinary incontinence happens when the muscular bladder wall contracts with extra force to overpower the sphincter muscles of the urethra, causing leakage or uncontrolled urination. 
This abnormal behavior of the bladder is termed as overactive bladder condition.

</p>

<p>
  The reasons for an overactive bladder may be one or more of the following:
</p>
<p>
<li>The bladder controlling nerves may be damaged or not functioning properly due to diseases such as stroke, Parkinson's disease, multiple sclerosis, etc. </li>
<li>Degenerated facet joint in the spinal cord.</li>
<li>Bladder irritation.</li>

</p>

<p>Urinary incontinence can be a mild and occasional or chronic uncontrolled type. 
</p>

<h2 class="blogh2">
Stress incontinence

</h2>
<p>This is also a major cause of urinary incontinence that occurs when there are muscle contractions on the bladder due to exercise, lifting of weights, coughing, sneezing, or laughing.</p>
<p>
Stress incontinence is the most common type of bladder control problem among young and middle-aged women. Stress incontinence in the middle-aged women often starts during menopause.
</p>

<h2 class="blogh2">Who are at risk of getting urinary incontinence</h2>
<p>
  <ul>
<li>Older adults, nearly 65 years of age.</li>
<li>People who are obese.</li>
<li>Women with a record of C-section or other pelvic surgery.</li>
<li>Men with prostate conditions or prostate surgery.</li>
<li>People with diabetes, stroke, or injury. </li>
<li>People with conditions such as <a href="https://cmclancaster.com/blog/urinary-tract-infection/" target="_blank">urinary tract infections</a>, <a href="https://cmclancaster.com/blog/bladder-cancer-survival-rates/" target="_blank">bladder cancer</a>, and prostate cancer. </li>
</p>


