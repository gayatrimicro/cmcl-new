                      <h1 class="blogh1">Bariatric surgeries giving new lives to morbidly obese people</h1>
                    
                     <p>Obesity, especially morbid obesity, is not always a self-inflicted condition. There are large number of citizens who helplessly watch their body mass index going alarmingly upward in spite of their best efforts, causing depression, low self-esteem, shame and social rejection for them - Bariatric surgery is probably the best thing that can happen to them to achieve considerable weight loss and a normal life.
                     </p>
                     <h2 class="blogh2">
                       What is bariatric surgery?
                     </h2>
                     <p>This is a surgery procedure performed on stomach and/or intestine, regarded as the ultimate in the treatment of extreme obesity. People with body mass index between 35 and 40 or higher, and with the risks of type 2 diabetes or heart disease make perfect cases for bariatric surgery. 
The concept is built around restricting the size of the stomach or physically removing parts of the digestive tract in order to limit the food intake and absorption of calories.
</p>
<p>
  Presently there are five different ways the bariatric surgery can be performed, depending upon the condition of the patient: 

</p>
<p>
  <ul>
  <li><b>Restrictive surgery</b> - done on upper portion of the stomach where esophagus joins the stomach, to limit the amount of food intake by reducing the size of the stomach, also known as Gastric sleeve or Gastric band surgery.</li>
<li><b>Malabsorptive surgery</b> - done on the small intestine to limit the amount of food absorption in the intestinal tract by bypassing a portion (shortening) of the small intestine.</li>
<li>
<b>Malabsorptive cum Restrictive surgery</b> - Combination of both techniques, also called Gastric bypass surgery.</li>
<li><b>Duodenal switch (DS) surgery</b> - Combination of stomach size reduction, intestine shortening, and removal of the gallbladder.</li>
<li><b>Biliopancreatic diversion surgery</b> - Very complicated surgery, generally recommended for the patients with BMI 50 or more. In this surgery the stomach is reduced to a small pouch by removing the maximum portion of it and additionally the portion of small intestine is bypassed.
</li>
</ul>
</p>
<h2 class="blogh2">
  How do the bariatric surgeries work?
</h2>
<p>
  <ul>
<li>The restrictive surgery reduces stomach size substantially, by up to 80%</li>
<li>Since the size of the stomach is reduced, eating becomes less and the patient feels full at less eating.</li>
<li>Smaller stomach secretes less hunger causing hormones so the patient feels less hungry.</li> 
<li>Physically removing parts of the small intestine results in less calorie absorption.</li>
</ul>
</p>
<h2 class="blogh2">
  Bariatric diet and eating methods before and after the surgery.
</h2>
<p>
  Getting proper diet before surgery will reduce complications during surgery and bring down the BMI of the patient. Proper post surgery diet provide the required energy and nutrients to heal from the surgery and nutrition make up for the extreme weight loss. The right diet practice involves:</p>
<p>
  <ul>
<li>High protein and low carbohydrate foods are ideal before the surgery.</li>
<li>Unhealthy meal before the surgery is strictly avoidable.</li>
<li>Slowly eating of foods in small portions after chewing thoroughly.</li>
<li>Abundance of sugar-free liquids but not shortly before or after the surgery.</li>
<li>No or minimal snacking.</li>
<li>Religiously adhering to the vitamin intake routine.</li>
<li>Avoiding calorie dense tasty foods, made of sugar and fat.</li>
<li>Avoiding alcohol.</li>
</ul>
</p>
<h2 class="blogh2">
  Benefits of bariatric surgery
</h2>
<p>
Besides long lasting weight loss which is around 70% - 75% less than the weight before the surgery, Bariatric surgery has other health benefits too, such as -</p>
<p>
  <ul>
    
<li>Sustaining reduction of type 2 diabetes</li>
<li>Improved heart conditions</li>
<li>Relief from depression</li>
<li>Improved sleep by eliminating obstructive sleep apnea.</li>
<li>Relief from joint pain due to reduction in excessive loads on the joint bones</li> 
<li>Improvement in fertility prospects</li>
<li>Improvements in host of other problems due to overweight.</li>

  </ul>
</p>
