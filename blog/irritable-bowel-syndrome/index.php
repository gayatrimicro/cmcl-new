                     <p>Irritable Bowel syndrome or IBS is a <a href="../gastrointestinal-diseases/">chronic gastrointestinal disorder</a> that afflicts a large number of people across the globe, aged or children, men or women. The common signs and symptoms of IBS include - </p>
                     <p>
                      <ul>
<li>abdominal pain</li>
<li>altered bowel habits - </li>
<li>increased flatulence and bloating</li>
<li>cramping</li>
</ul>
<p>
People suffering from IBS are generally food intolerant which means that they are abnormally sensitive to a lot of foods; these are not allergic conditions. Intake of these foods (foods vary with people and of course quantity matters) invariably result in swelling of guts with gas or fluid. </p>
<p>
There are two types of IBS that divides the IBS patients as specific to one type or the other, referred to as IBS-D and IBS-C. IBS-D people are characterised by chronic or recurrent diarrhea while the IBS-Cs experience frequent abdominal pain or discomfort associated with constipation. However, there are some people who experience both IBS-D and IBS-C.</p>
<p>
Since the exact cause for IBS is still unknown there is no cure to this problem. However, there are treatments available to reduce or eliminate symptoms which include changes in diet and lifestyle, and prescription medicines.</p>
<p>
Since, different people are intolerant to different foods it is very important for the IBS patients to identify the foods that trigger their IBS condition, so they can avoid intake of such foods. However, as a thumb rule, increased consumption of dietary fibres, drinking plenty of water, avoiding soda, and eating small amount of foods at a time help alleviate the symptoms. 
There is no set treatment that works for everybody. It is wise to talk to a primary-care physician or a gastroenterologist about the best way to manage IBS symptoms for individual.
</p>
<p>
Irritable bowel syndrome (also known as spastic colon or mucous colitis or nervous colon)  hampers the normal functioning of the digestive system, resulting in the symptoms of IBS. This means that IBS is a movement problem only. There is no damage to the GI tract in IBS, in contrast with the other <a href="../gastrointestinal-diseases/">GI tract problem</a> which is caused by the inflammation and damage of the GI tract. </p>


                     <h2 class="blogh2">
                    Recommended IBS diets
                     </h2>
                     <p>Certain foods can improve IBS condition in some people but may not work for others. You should choose your foods by observing your symptoms and talking to a gastroenterologist. Below are some insights about different diet types that may help you increase your awareness. </p>


</p>


  <h3 class="blogh2">High fiber diet</h3> 
  <p>Fiber rich foods aid bowel movement. Whole grains, fruits, and vegetables are nutritious and reduce constipation. However, increased fiber in diet sometimes causes bloating. If you experience bloating from your high fiber diet, reduce whole grain fiber in the diet and increase soluble fiber found in fruits and vegetables. 
</p>

<h3 class="blogh2">Low fiber diet</h3>
 <p> For some people, who have frequent gas and diarrhea, increasing fiber in diet may worsen the symptoms. If you have similar problems from high fiber intakes, reduce taking insoluble fiber and concentrate more on eating fruits and vegetables rich with soluble fiber. Taking antidiarrheal medicines half-an-hour before eating also can help to reduce the effects. This can be a good solution to occasional eating in restaurants or when you are on the go. 
</p>

<h3 class="blogh2">Gluten-free diet</h3> 
<p>
Gluten is the naturally occurring protein found in some grains such as wheat, rye, spelt, and barley. Some people are not gluten tolerant and can experience celiac disease related to IBS. Avoid eating foods made of gluten rich grains such as bread and pasta to check if your symptoms are reduced.</p>

<h3 class="blogh2">Low-fat diet</h3>
<p>
Foods with high fat content are generally low in fiber which create problems for people with IBS. Doctors and dieticians say that fatty foods must be avoided by people with mixed IBS (IBS-C and IBS-D). Eating low fat foods is likely to improve IBS condition and is also good for heart. </p>
<p>
Shun red meats and deep fried foods, try to eat more of lean meats, fruits, vegetables, gluten free grains, and low fat dairy products.</p>

<p>
As a thumb rule, drinking plenty of water, regular exercise, and decrease your caffeine intake help minimize your IBS symptoms.
  </p>



  <h2 class="blogh2">Is IBS a serious illness?</h2>
  <p>

No and yes. Many people with mild IBS accept this as part of their daily lives and donâ€™t let the symptoms get into their skin. But for others, IBS may be a big discomfort that may cripple their day to day works and severely affect their quality of life. The most irritating characteristic of this condition is its unpredictability, both in terms of occurrence and response to the treatment.  </p>

<h2 class="blogh2">How do menstrual cycles affect IBS symptoms?</h2>
  <p>

Bowel system functions are evidently influenced by the changes in female hormone level. Conditions may get worse during the menstrual period. Whether with IBS or not women in general are reported to have irregular bowel functions which becomes somewhat acute, prior to and during the menstruation period, causing abdominal pain and bloating.</p>

<p> 
This biological characteristic of women can make it difficult for the doctor to diagnose IBS in a woman as the similar symptoms may occur due to some gynecological problems as well.  </p>

<h3 class="blogh2">Treatment</h3>

<p>
Treatment of IBS depends upon the symptom experienced by the patient. Type of medications applied to deal with various IBS symptoms include anticholinergics, antidiarrheals, tricyclic antidepressants, prokinetic agents, bulk-forming laxatives, serotonin receptor antagonists, chloride channel activators, and guanylate cyclase C (GC-C) agonists.
It has been observed that several antispasmodics, including peppermint oil, pinaverium, trimebutine, and cimetropium/dicyclomine substantially improve irritable bowel syndrome symptoms. </p>




