                      <h1 class="blogh1">Surprising Facts On Controlling Asthma to Breathe Easier</h1>
                    
                     <p>You, I, and many others have at least one or two friends or relatives who are asthmatic. They are perfectly healthy externally, but vulnerable to dust, smoke, and a lot of things that trigger their respiratory problem. A small handheld inhaler is their essential companion. When under attack they overcome their breathing difficulties by pumping in medicines through their mouth.
                     </p>
                     
                     <p><a href="http://globalasthmareport.org/" target="_blank" rel="nofollow"> According to the Global Asthma Report 2018</a>, asthma kills around 1000 people every day and affects as many as 339 million people - and prevalence is rising. </p>

<h2 class="blogh2">
  What is Asthma?
</h2>
<p>
  Our respiratory system sucks in air from outside through nose or mouth and channelises it to the lungs using the airways called bronchial tubes. The bronchial tubes are also used by the lungs to exhaust out the used up air from the body. 
</p>
<p>Asthma is a chronic respiratory problem that is triggered by certain external causes or conditions, that may vary from person to person. The bronchial tubes in the lungs get choked either by constriction or by excessive mucus.</p>
<p>The bronchial tubes which are absolutely vital for the inflow and outflow of air to and from the lungs get inflamed. This causes narrowing of the air path that makes breathing very difficult. </p>
<p>The duration of an attack depends upon the cause of the attack and may vary from a few minutes to a few hours or even days. The common symptoms of asthma are wheezing and rattling sound in the chest.</p>
<h2 class="blogh2">
  Causes:
</h2>
<p>
  In a large number of people, the asthma attack is triggered by allergens such as a tree, grass, or weed pollens, dust mites, cockroaches, pigeons, pets, etc. Other factors for asthma include the irritants in the air, such as smoke or chemical fumes, strong perfumes, etc.     </p>
<p>
 Illnesses such as flu, sinusitis, or even a mild respiratory infection can also be responsible for an asthma attack. Some of the uncommon causes of asthma include strenuous exercise, extreme weather conditions, etc.
</p>
<h2 class="blogh2">
  Asthma in children 
</h2>
<p>
Although asthma can appear in people of any age, children are more susceptible to asthma than elders. In fact, asthma is a common chronic disease among children. Children with low birth weight, those exposed to tobacco smoke, are black and raised in a dingy and congested environment can develop asthma fast. 
</p>
<p>
  Parents with asthma and allergies are also important factors for children to develop asthma. Mostly, asthma affects a child at around five years of age with symptoms of wheezing and breathing trouble. Here are some of the common triggers for asthma attacks in children -

  <ul>
    
<li>Cold air, changes in weather</li>
<li>Smoke</li>
<li>Allergens, such as pollens, animal dander, dust mites, etc.</li>
<li>Strong odors of paints, perfumes, and other chemicals.</li>
<li>A fit of laughing or crying</li> 
  </ul>
</p>

<h2 class="blogh2">How do you know if you have got an asthma attack?</h2>
<p>An asthma attack happens when the bronchial tubes either get narrowed by the inflammation or clogged by the excessive mucus. Below are the common symptoms of an asthma attack -

  <ul>
    <li>Difficulty in breathing,</li>
<li>Loud wheezing sound during breathing,</li>
<li>Non-stop coughing,</li>
<li>Very rapid breathing,</li>
<li>Pressure on chest and feeling like retraction (tightening of chest muscles),</li>
<li>Sweating of face and body,</li>
<li>Lips and fingernails turned blue
 
</li>
  </ul>
</p>


<h2 class="blogh2">Treatment</h2>
<p>Asthma is a long-term chronic disease which doesnâ€™t have any therapeutic cure as of date. It is the awareness and control of the disease that helps a patient avert or minimize the symptoms. There are some tools or medicines, as mentioned below, that help you keep your asthma condition well under control.</p>

<h2 class="blogh2">Peak Flow Meter</h2>
<p>A peak flow meter is a small handheld device which measures how well your lungs can exhale air. This gives you the status of your bronchial tubes. Your doctor will advise how often the test is to be conducted and how to interpret the readings of the test to decide on the amount of medications. Depending on necessity the test can be carried out every day or intermittently.</p>

<h2 class="blogh2">Spirometer</h2>
<p>A spirometer is a device which is used by the doctor to measure how much air your lungs can hold and how much air the lungs are able to exhale in one second. Handheld <a href="https://www.asthmafoundation.org.nz/your-health/living-with-copd/what-is-spirometry" target="_blank" rel="_blank">spirometers</a> are also available to conduct the test at home. </p>

<h2 class="blogh2">Asthma control medications</h2>
<p>There are two types of medications to tackle asthma - long-term control medicines and quick-relief medicines. Most medications are available in powders or mists which are taken orally using a handy inhaler. Some medicines are taken in the pill form.</p>
<p>
Long-term control medicines help prevent asthma symptoms such as airpath inflammation and swelling. Daily Inhaling of corticosteroids greatly reduce the symptoms.</p>
<p>
Of the quick-relief medicines, most common are inhaled short-acting B2- agonists - bronchodilators that quickly relax the tight muscles around the bronchial tubes, allowing more flow of air through them.
</p>

