                     <p>Crohn’s disease is a chronic disorder that occurs due to inflammatory condition in any part of the gastrointestinal tract, which is why it is also known as a type of Inflammatory Bowel Diseases (IBD). The symptoms of the disease include -</p>
<p>
  <ul>
<li>Persistent diarrhoea, </li>
<li>Abdominal pain, </li>
<li>Bleeding from the rectum, </li>
<li>Fever,</li>
<li>Weight loss. </li></ul></p>

<p>
The symptoms can continue for a period before occurring again after a break of weeks, months or even years. The symptom depends upon in which area of the bowel system  the disease happens. </p>
<p>
Although, Crohn’s disease can affect any area starting from mouth to anus the most vulnerable area is the lower part of the small intestine called the ileum. While the disease can occur at any age it mainly affect the young people. </p>

<h1 class="blogh1">
 What causes Crohn’s disease?
</h1>
<p>
  The cause of the Crohn’s disease is yet to be known. It is only assumed to be due to an abnormal reaction by the body’s immune system. Another theory suggests that certain bacterial infection, such as strains of mycobacterium, can cause Crohn’s disease. </p>
<p>Also, there is strong likelihood of Crohn’s diseases (a type of IBD) run in families, suggesting a genetic basis for the disease. Ten to twenty percent of the IBD patients in the USA found to have a family history of IBD, with most got it from one of their first degree relatives( mother, father, sister , or brother ). You are at ten times greater risk if any of your parents or siblings is detected with the disease.</p>
<p>
However, Crohn’s disease is not contagious and has got very little to do with diet, despite the fact that diet may sometimes affect the symptoms of it.
The diagnosis of the crohn’s disease involves physical exam, lab tests, imaging tests, and colonoscopy.</p>
<h2 class="blogh2">
  Treatments:
</h2>
<p>
  As of date there is no permanent cure for Crohn’s disease. Patients typically experience two phases - relapse and remission - that come alternately. The relapse phase refers to worsening of inflammation, during when the symptoms of the disease (abdominal pain or diarrhea or rectal bleeding, depending upon the affected area) are at their peaks. During the remission phase which comes after the relapse phase and can be of any length, months or even years, the condition of the patient improves</p>
  <p> 
The treatments that include medications or surgery, bring about the remission phase by improving the symptoms and healing the patient. However, the remission phase may start on its own, without any treatment. </p>
<p>
Since, Crohn’s disease cannot be cured the treatments are aimed at early induction of remission phase and lengthening it as much as possible with minimal side effects from the medicines. The quality of life of the patient is improved during the remission phase.
</p>
<h2 class="blogh2">
 Medication -
</h2>
<h3 class="blogh2">Anti-inflammatory drugs -  </h3>
<p>
These drugs are generally the first therapy applied on a patient which include -</p>
<p><ul>
            
            <li><a href="https://medbroadcast.com/channel/crohns-colitis/treating-crohns-disease/5-asa-medications" rel="nofollow" target="_blank"> 5 ASA,</a></li>
            <li><a href="http://www.crohnscolitisfoundation.org/resources/corticosteroids.html" rel="nofollow" target="_blank">Corticosteroids</a></li></ul></p>


<h3 class="blogh2">Immune system suppressors -</h3>
<p>These drugs reduces inflammation by suppressing the abnormal response of the immune system. In some cases a combination of immunosuppressant drugs gives better results. These drugs include -</p>
<p>
  <ul>
<li><a href="https://www.sciencedirect.com/topics/medicine-and-dentistry/azathioprine" rel="nofollow" target="_blank"> Azathioprine</a> (Azasan, Imuran) and <a href="https://www.svhm.org.au/ArticleDocuments/2305/Mercaptopurine.pdf.aspx?embed=y" rel="nofollow" target="_blank">mercaptopurine</a> (Purinethol, Purixan).</li>
<li><a href="https://www.rxlist.com/consumer_cyclosporine_gengraf_neoral_sandimmune/drugs-condition.htm" rel="nofollow" target="_blank">Cyclosporine</a> (Gengraf, Neoral, Sandimmune)</li>
<li><a href="https://www.nice.org.uk/guidance/ta329" rel="nofollow" target="_blank">Infliximab (Remicade), adalimumab (Humira) and golimumab (Simponi).</a> </li>
<li><a href="https://inflammatoryboweldisease.net/treatment/medications/entyvio/" rel="nofollow" target="_blank">Vedolizumab (Entyvio)</a></li>

  </ul>
</p>


<h3 class="blogh2">Other medications</h3>
<p>Other medications are required to take care of the specific symptoms of Crohn’s disease such as -</p>
<p>
  <ul>
<li>Antibiotics</li>
<li>Anti-diarrheal medicines</li>
<li>Pain relievers</li>
<li>Iron supplements</li>
</ul>
</p>
<p>
Selection of treatment depends on factors like severity of disease, location of disease, and the complications associated with disease. Biologic response modifiers or biologics are increasingly used in the treatment of Crohn’s disease that target the body’s main inflammatory producing enzymes. Biologics which are popularly used to treat crohn’s disease include -</p>
<p>
<ul>
<li><a href="https://inflammatoryboweldisease.net/treatment/medications/cimzia/" target="_blank" rel="nofollow"> Cimzia®</a></li>
<li><a href="https://www.humira.com/crohns" target="_blank" rel="nofollow"> Humira®</a></li>
<li><a href="https://www.simponi.com/psoriatic-arthritis/what-is-simponi" target="_blank" rel="nofollow"> Simponi®</a></li>
<li><a href="https://www.stelarainfo.com/" target="_blank" rel="nofollow"> Stelara®</a></li></ul> </p>

<p>
But one should be very careful while using these other medicines, biologics and must consult with the doctors before using them.
</p>

