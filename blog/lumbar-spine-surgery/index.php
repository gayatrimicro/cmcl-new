                      <h1 class="blogh1">Lumbar Surgery can be a long term solution to your severe back pain.</h1>
                    
                     <p>As evidenced by its high rate of occurrence, our lower back can turn into a major source of pain and discomfort after the age of 30. Also known as lumbar, this region is built around the lowest part of our spine and is the place of immense importance because:</p>
<p> 
1. The lumbar spine is connected to pelvis where most of our weight bearing and body movement takes place. Activities such as lifting up a heavy box, carrying a heavy object, twisting to move a heavy load, etc. put too much pressure on our lower back or lumbar.</p>
<p>2. The lumbar spine is also the area that houses the junction point where the cauda equina or the bunch of nerves corresponding to our lower limbs, pelvis, and bladder meets the spinal cord for communication with our brain.

</p>

<h2 class="blogh2">
 Types of lower Back pain 
</h2>
<p>
 Most back pains are mechanical pains that can vary from mild and annoying to severe and debilitating. They may come and go for sometimes and gradually get worse. There can be a variety of symptoms depending on the underlying cause of the pain. For example:
</p>
  <p>
    <ul>
<li>Dull or achy pain at the low back.</li>
<li>Sciatica i.e. numbness or tingling of the lower legs and feet. </li>
<li>Stinging, burning pain that shifts from the low back to the backs of the thighs. </li>
<li>Stiffness and muscle spasms in the low back, pelvis, and hips.</li>
<li>Intense pains after prolonged sitting or standing.</li>
<li>Difficulty standing up straight, walking, or going from standing to sitting.</li>


</ul>

</p>
<h2 class="blogh2">Lumbar spine surgical procedures for pain relief
</h2>

<p>Surgeries provide substantial pain relief and are often opted for when all the non-surgical therapies have failed. </p>
<p>
At CMC Lancaster, we offer minimally invasive spinal surgeries performed by our reputed spine surgeons. In a <a href="https://cmclancaster.com/blog/laparoscopy-minimally-invasive-surgery/" target="_blank"> minimally invasive surgery</a> surgeons use instruments called laparoscope by making minimum incisions to ensure the following benefits over open surgeries: </p> 
<p>
  <ul>
<li>Risk-free complex operation under local anesthesia. </li> 
<li>Minimum chance of infection and blood loss.</li>
<li>Same day or next day release.</li>
<li>Fast recovery and return to normal life.</li>

  </ul>
</p>
<p>It is the Lumbar decompression surgery that is performed to treat back pain. Lumbar decompression surgery can be performed by three basic methods depending upon the affected part of the vertebrae. They are <b>Laminectomy, Discectomy,</b> and <b>Spinal fusion.</b> </p>
<h3 class="blogh3">Laminectomy</h3>
<p>This method is performed to treat the condition called nerve root compression ( also known as a pinched nerve or herniated disc ) by removing a section of bones called lamina from the posterior of a lumbar vertebra. The procedure takes pressure off the spinal cord which was punched or compressed due to the displaced lamina. 
</p>
<p>There are five vertebrae in the lumbar region each with two laminas. A laminectomy can be performed at any vertebra using the minimally invasive method.</p><br>


