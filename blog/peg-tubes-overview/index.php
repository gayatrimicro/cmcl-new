                      <h1 class="blogh1">PEG tube feeding- a brief overview</h1>
                    
                     <p>It is said that the stomach is the gateway to our health where foods must reach on their way down the <a href="https://cmclancaster.com/blog/gastrointestinal-diseases/" target="_blank">digestive tract</a>. The path to the stomach comprises the mouth and a pipe called esophagus. </p>
                    

