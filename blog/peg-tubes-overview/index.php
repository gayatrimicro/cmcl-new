             <p>
But due to some diseases, there can be great difficulties sending the food morsels to the stomach. It may be very painful swallowing the food or there may be some problems in the esophagus. </p>
<p>
PEG feeding, also called <strong>percutaneous endoscopic gastrostomy</strong>, helps the patients survive when natural feeding is not possible. It provides an <a href="https://cmclancaster.com/blog/feeding-tubes/" target="_blank">alternative route</a> for foods to reach the stomach by bypassing the mouth and esophagus. It is a proven method used by healthcare providers during treatment if the patient isn’t able to eat. 
</p>

<h2 class="blogh2">
 How is a PEG tube inserted into the stomach?
</h2>
<p>
 The PEG tube is inserted with the help of an endoscope. An endoscope is a special instrument, shaped like a thin flexible pipe with a torch and a camera fitted at the leading end. The camera sends the picture of the inside of the stomach which is viewed on a monitor attached to the instrument.</p>

<p>
The <a href="https://cmclancaster.com/blog/endoscopy-procedure/" target="_blank">endoscope</a> is passed through the mouth into the stomach. The surgeon then makes a small incision through the skin into the stomach and inserts the PEG tube through the opening as guided by the camera output of the endoscope.</p>
<p> 
The patients are lightly sedated using midazolam. A special balloon or cap is used to hold the PEG tube inside the stomach. 
</p>


<h2 class="blogh2">How is the feeding done?</h2>

<p>Specialized liquid foods and fluids (water, etc.) are made to reach the stomach slowly through the PEG tube. Although some patients may try to eat or drink orally after the procedure, this must be discussed with the doctor.   </p>

<h2 class="blogh2">How to take care of the insertion site?</h2>
<p>A dressing is done on the PEG site after the procedure. The dressing is usually removed after a day or two, to be followed by simple, but careful cleaning once a day with diluted soap and water. No special dressing or covering is needed but the site must be kept dry between cleansing.  </p>







 

<h2 class="blogh2">Complications from PEG feeding</h2>
<p>Although the success rate of PEG feeding is very high (over 95 %) complications may still occur, such as pain at the PEG site, leakage of stomach contents seeping out to the tube site, and tube malfunction. These may cause infection of the PEG site. </p>
<p>
Other complications include pulmonary aspiration or inhalation of stomach contents into the lungs, bleeding, and perforation in the bowel wall. 
</p>


