                      <h1 class="blogh1">Vitamin and mineral supplements to help with your weight loss</h1>
                    
                     <p>Weight management programs are not quick fixes for obesity, but rather the first steps to shedding the bulk of the extra weight that should be followed by a regimen of exercise, balanced diet, and <strong>vitamin supplements.</strong></p>
   <p>                  
The medical weight loss & <a href="https://cmclancaster.com/services/physical-rehab/" target="_blank" >physical rehab</a> facility at the CMC Lancaster offers comprehensive follow-up care that includes vitamin supplementation among other services to maintain the level of essential nutrients in your body. You’ll receive a personalized program to achieve long-term freedom from obesity and a life full of vigor.  </p>
<p>
Here is a list of the vitamins and supplements that the doctors and nutritionists normally suggest to prevent nutrient deficiencies after your <a href="https://cmclancaster.com/blog/bariatric-surgery/" target="_blank">weight loss therapy.</a> 
</p>

<h2 class="blogh2">1. Multivitamin tablets </h2> 
<p>
  High potency, chewable multivitamin and mineral supplement that contains:</p>
  <ul>
 
<li>Iron - minimum of 18 mg </li>
<li>Folic acid - 400 mcg.</li>
<li>Selenium</li>
<li>Copper </li>
<li>Zinc</li>

</ul>
<p>Doses - as directed by the doctors.
</p>              


<h2 class="blogh2">
 2. Calcium

</h2>
<p>
 Minimum daily intake should be 1200 - 2000 mg as advised by the doctor. </p>
<p>Better results can be achieved if the calcium supplement tablets are taken two or three times a day, taking the total doses in smaller amounts. For example, 500 - 600 mg tablets three times a day. Calcium citrate is the preferred form of calcium. 

 </p>




<h2 class="blogh2">3. Magnesium</h2>

<p>The recommended dosage for men is 400 -420 mg and for women 310 -320 mg.</p>
<p>Magnesium helps to relax your muscles, makes you feel calm, and have good sleep. Lack of good sleep is a major cause of weight gain.
</p>

<h2 class="blogh2">4. Vitamin D</h2>
<p>Advisable daily intake of vitamin D is 800 - 1000 IU (International Units). The total amount should be taken in two installments daily of 400 - 500 IUs each, with your doses of calcium supplement. You can also take a combination calcium-vitamin D supplement of proper dosages to avoid taking multiple pills.
</p>

<h2 class="blogh2">5. Vitamin B12</h2>
<p>
Daily intake should be 500 mcg. These tablets can be crushed and ingested or kept under the tongue.</p>

<p>You may also need to consume iron or folic acid supplements. This is more likely if you are a female and still menstruating.
</p>


