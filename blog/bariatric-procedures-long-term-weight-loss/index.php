                      <h1 class="blogh1">Bariatric Procedures: Ways to Promote Long-term Weight Loss</h1>
                    
                     <p>If you or a loved one is severely obese and frustrated that all your efforts to lose weight
have failed to produce any long-term result, then bariatric surgery can be a great aide to you to achieve that and a lot more. Thousands of morbidly obese people have been able to transform their lives through successful bariatric surgeries.</p>
<p>
There are different types of weight loss surgery procedures, combinedly called bariatric surgeries. Of course like in any major surgery, there can be complications from the bariatric surgeries as well, and also your conditions should justify going for the procedure. 
Nevertheless, the prospect of massive and sustained weight reduction with surgery is in sharp contrast to the experiences patients commonly have from non-surgical therapies.</p>
<p>
There are two main methods of bariatric surgeries that promote long-term weight loss, known as  restrictive and malabsorptive surgeries. The leading forms of weight loss surgery as listed below, use either one of these two methods or combinations of them:
                     </p>
                     <p>
                       <ul>
<li>Gastric band surgery</li>
<li>Sleeve gastrectomy surgery</li>
<li>Gastric bypass surgery</li>
<li>Duodenal switch surgery</li>
<li>Biliopancreatic diversion procedure</li>

                       </ul>
                     </p>

                <p>
                  Restrictive surgeries are done on the upper part of the stomach where <a href="https://www.msdmanuals.com/home/digestive-disorders/esophageal-and-swallowing-disorders/overview-of-the-esophagus" target="_blank" rel="nofollow"> esophagus</a> joins the stomach, to limit the amount of food intake by reducing the size of the stomach.</p>
                  <p>

Malabsorptive surgeries are done on the small intestine to limit the amount of food absorption in the intestinal tract by bypassing a part (shortening) of the small intestine.
                </p>     
                    
                     <h2 class="blogh2">
                       Gastric band surgery 

                     </h2>
                     <p>This is a type of restrictive surgery method of weight loss which is minimally invasive, quick, reversible, and adjustable procedure that makes the stomach size smaller to restrict intake of food.</p> 
<p>In this method, the stomach is made smaller by squeezing it around the top, using an inflatable silicone band. So the person feels fuller with less food. After surgery, the patient must follow a strict liquids-only diet with gradual reintroduction of soft foods for up to 6 weeks. </p>
<p> 
This method, approved for use as a weight-loss treatment by the U.S. Food and Drug Administration (FDA), can result in significant weight loss and improve diabetes. Since the food intake is small and mostly one time, all food consumed is digested and absorbed normally. There is no malabsorption.
</p>

<h2 class="blogh2">
  Sleeve gastrectomy surgery 

</h2>
<p>
 Also known as vertical sleeve gastrectomy or gastric sleeve procedure, this method requires a surgeon to remove a large portion of your stomach, leaving a small portion( the size of a banana) for use along with the pylorus, the muscle that controls emptying of food from the stomach into the intestine.</p>
<p>The important point is that in this method the surgeon removes the portion of the stomach that produces a hormone that is responsible for making a patient feel hungry.purely restrictive procedure.</p>
<p>
Sleeve gastrectomy is a simpler procedure than the gastric bypass operation because it does not require the use of banding device and does not involve rerouting of or reconnection of the intestines. 

 
</p>



<h2 class="blogh2">
  Gastric bypass surgery 
</h2>
<p>
  This is an example of a combination method that is restrictive as well as malabsorptive surgery method that helps you lose weight by altering your process of digestion.</p>
  <p> 
The food you eat will no longer go into some parts of your stomach and small intestine that absorb nutrients from the food. Because of this, your body will not get all of the calories from the food you eat.</p>
<p>
There are multiple types of gastric bypass procedures that involve both bypassing part of stomach and part of the small intestine by greater or lesser degrees, which is why they are called both restrictive and malabsorptive. 
</p>
<h2 class="blogh2">
 Duodenal switch(DS) surgery 

</h2>
<p>
This is again a combination type that employs both restrictive and malabsorptive methods to achieve substantial long-term weight loss. The best part that makes the DS surgery method popular is that this allows the patient to eat a near-normal meal(in much smaller quantities) and also achieve their weight loss goal.
</p>
<p>
In this method, the surgeon permanently removes the majority of the most stretchable part of the stomach and the upper small intestine leaving only the basic stomach structure. In addition, roughly two-thirds to three-fourths of the upper small intestines is also removed, resulting in a significant decrease in calorie absorption. Weight loss is achieved by the dual effects. 


</p>
 <h2 class="blogh2">         
Biliopancreatic diversion procedure
</h2>
<p>
This is the third type of combination method( 2-stage operation), applied on the patients with a BMI of more than 60.
</p>
<p>
In the first stage, a new stomach pouch is created by using only a small portion of the original stomach( about a quarter of its original size ). The remaining portion is removed from the body.
</p>
<p>
In the second stage, a portion of the small intestine is bypassed by cutting this part away and attaching the new stomach pouch to the other part of the small intestine. Food will now pass from your pouch into this section of small intestine.</p>
<p>
The bypassed part which was earlier connected to the bottom of the original stomach now joins the other part of the small intestine close to the large intestine. This is done to make sure that the digestive juices mix with food only at the end of the small intestine, thus resulting in minimum absorption of food.


</p>
<h2 class="blogh2">    
Wrap up</h2>
<p>
While the right method of weight loss surgery for you will depend upon your unique conditions, you can expect a sustainable weight loss of 60- 80%. However, you will have to change your eating habits and engage in regular exercises in order to maintain your gain.
</p>




