                      <h1 class="blogh1">ACL repair helps your speedy return to the sport after a painful knee injury.</h1>
                    
                     <p>There are probably fewer injuries more distressing to a sportsperson than an ACL tear because it keeps him/her away from the sport for a long time. </p>
<p>
The anterior cruciate ligament or ACL is a major tissue, critical to the stability of our knee because with the PCL (posterior cruciate ligament) it ties the thigh bone and the shin bone together. While PCL injury is rare, the ACL injury or tear accounts for nearly 50% of all leg injuries occur in sports.</p> 
<p>
Both ACL and PCL are responsible for sliding the lower leg forward and backward in order to generate movements. ACL, in particular, provides the stability of the knee joint, especially during quick side-to-side motions. 
</p>
                    


<h2 class="blogh2">
 Symptoms of an ACL tear
</h2>
<p>
 A torn ACL is usually a 2nd-degree or a 3rd-degree sprain that refer to a partially torn or a completely torn ligament. You may experience:</p>
 <p>
   <ul>
<li>Sudden pain, moderate to severe.</li>
<li>A loud pop or snap during the injury.</li>
<li>Swelling of the knee within the 1st 24 hours of injury.</li>
<li>Difficulties in straightening the knee or walking.</li>
<li>Inability to weight bearing.</li>

   </ul>
 </p>




<h2 class="blogh2">Treatments</h2>

<p>Common wisdom supported by medical observation, is that the torn ACL simply cannot heal on their own, probably because the torn sides of the ligament aren’t able to get closer to fasten themselves back into one unit. </p>

<h2 class="blogh2">ACL reconstruction</h2>
<p>The only treatment option for a torn (or stretched beyond its limit) ACL till recently was reconstructive knee surgery. This would involve grafting of tendons to replace the torn ligament until the creation of new ligament by the body. </p>
<p>
The ACL reconstruction is a complicated process and it usually takes an athlete nearly a year or so to get back to the sport. Studies indicate that ACL reconstruction may lead to a decrease in the strength of the knee and also to arthritis over time.  </p>

<h3 class="blogh3">ACL repair</h3>
<p>
A new method of surgical treatment has emerged to heal the torn ACL from the femoral side is ACL repair. This has been possible due to improved imaging technique that helps to identify the tear location at the femoral side clearly. </p>    

<p>This is a <a href="https://cmclancaster.com/blog/laparoscopy-minimally-invasive-surgery/" target="_blank">minimally invasive approach</a> that repairs the torn ACL instead of using a tendon graft. The procedure is focused on maintaining the native tissue of the patient. The surgeon creates small incisions of typically 5-7 mm in diameter in the knee to repair the torn ACL. Further, a high tensile-strength braided suture is passed through the repaired ligament to provide additional support and strength. </p>

<p>ACL repair results in no or minimal atrophy, minimum pain, and more rapid functional recovery. The time taken for the surgery is much less and, in most cases, the patients get released on the same day or the next day.</p>

<p>However, ACL repair isn’t appropriate for all patients. At CMC Lancaster, our highly experienced ACL specialists will check your ACL tear meticulously and suggest whether the ACL repair would be suitable for your case or the traditional ACL reconstruction. Both types of surgical procedures are performed with the necessary diagnostics at the CMC Lancaster.</p>


