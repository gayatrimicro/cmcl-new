                      <h1 class="blogh1">Nutritional counseling for your faster recovery and wellbeing strategy</h1>

                      <h2 class="blogh2">Why do we fall ill and how do we get cured? </h2>
                    
                     <p>The answers are simple - we fall ill when certain parts of our body fail to work properly and we get cured by taking the correct medicines. But are these answers complete and holistic? Perhaps not, because they are limited to individual illnesses and their treatments and don’t go beyond. </p>
<p>
Our medical training makes us address a disease at an ad-hoc basis that involves diagnosing and treating a medical condition by its current symptoms. You have a migraine, take a migraine pill. You have depression, take an antidepressant... like that. The patients many times have to go through multiple treatments with different medications at the same time. Chronic diseases are treated with regular medications that lead to some side effects. </p>
<p>
What if your medical conditions could be preempted or if you could reduce dependence on medicines and still live a healthy life keeping your disease under control? Sounds unreal or hoax? No, it is not. Nutritional counseling makes it possible by detoxing and optimizing nutrition levels in your body. 
</p>





<h2 class="blogh2">
What is nutritional counseling?</h2>
<p>
 This is a new type of treatment approach that is gaining momentum to complement traditional treatment. It is based on a thorough assessment of the patient’s body which analyzes deficiencies of the essential vitamins and minerals that constitute the building blocks for the different parts of the body and rectify them through proper food and exercise.</p>
<p>
Foods can be our medicines. Nutrition supplements through a strategic diet can prevent, reverse, and even cure diseases. To treat a chronic disease successfully, it is critical to get to the root cause. 
Proper analysis and a balanced diet of functional foods can help the patient get rid of the deficiency and heal. </p>
<p>
But you’ll need a nutrition specialist to help you with this.</p> 

<p>
At the CMC Lancaster, we use all methods to pinpoint the fundamental problem and work out an effective strategy that will be unique to the patient. Our solutions include foods that are simple, tasty, and affordable so that the patient gets engaged to it easily. 

</p>

<h2 class="blogh2">Are you the right candidate for nutritional counseling? 
</h2>
<p>Very much! Virtually anybody can be benefitted from nutritional counseling. People of all ages and categories such as pregnant women, vegetarians, and people with different medical complications have been assisted by our counseling.</p>


