                      <h1 class="blogh1">Nuclear medicine - an overview
</h1>
                    
                     <p>A nuclear medicine test does not mean you are having a fatal disease, rather it can give you a big relief from doubt and fear. This often complements CT-scan or MRI test to perform a practice called image fusion for correct diagnosis of your condition.</p>





<h2 class="blogh2">
 What is nuclear medicine?

</h2>
<p>
  Nuclear medicine is a branch of diagnostic and therapeutic services that are performed by using small amounts of radioactive materials. But don’t worry, this technique is 100% safe because the amount of radiation it uses is not enough to hurt you. 
</p>
<p>A nuclear medicine procedure reveals the molecular level information of the tissue cells that help doctors pinpoint if the tissue is diseased and what’s the stage. A 3-D anatomical image of the suspected tissue is produced that carries unique information about the metabolic status of the cells in them to unveil whether they are benign or have turned malignant.
</p>

<p>This is a painless process because of its non-invasive nature except for an injection in the beginning, to insert the radiotracer into the body. 
</p>


<h2 class="blogh2">How does nuclear medicine work?  
</h2>
<p>The amount of radioactive material (also known as a radiotracer) to be used vary from person to person. The concept is based on the fact that tumorous tissues grow fast and require extra glucose. The patient is typically injected with glucose mixed with radiotracers(radioisotopes) that travel with the blood through the suspected tissue.</p>
<p> 
As the glucose is absorbed by the tumorous tissues, the isotopes decay and start emitting radioactive rays. The detectors placed around the patient will collect the rays to produce the image of the suspected area.  

</p>
<p>Radioactive techniques are also used in treatments of cancer, thyroid, and other conditions. For example, radioactive iodine(I-131) is being used for a long time to treat thyroid cancer, hyperthyroidism, or an overactive thyroid condition.  Presently, this method is also being used in the treatments of <a href="https://www.cancer.org/cancer/non-hodgkin-lymphoma/about/what-is-non-hodgkin-lymphoma.html" target="_blank" rel="nofollow">non-Hodgkin lymphoma</a> and bone pain from some types of cancer.   </p>


<h2 class="blogh2">
Types of nuclear medicine imaging methods</h2>
<p>
 There are two types of imaging methods that use nuclear medicine technique. They are the PET(positron emission tomography) and SPECT(single-photon emission computed tomography).</p>
 <p>
The main difference between <a href="https://www.radiologyinfo.org/en/info.cfm?pg=pet" target="_blank" rel="nofollow">PET</a> and <a href="https://mayfieldclinic.com/pe-spect.htm" target="_blank" rel="nofollow">SPECT</a> is in the types of radiotracers and detectors used. While SPECT uses the emission of gamma rays collected by gamma cameras, the radiotracer used in PET scans leads to the emission of positrons that are collected by the suitable detectors.  </p>
<p>
PET produces a better image and is more expensive of the two. 
Nuclear medicine imaging is used to diagnose conditions related to the heart, lungs, bone, brain, <a href="https://cmclancaster.com/blog/gastrointestinal-diseases/" target="_blank">GI tract</a>, <a href="https://cmclancaster.com/blog/urinary-tract-infection/" target="_blank">urinary tract</a>, renal, spine, and other areas. They are particularly helpful in diagnosing cancer, thyroid, heart and gallbladder diseases along with <a href="https://www.alz.org/alzheimers-dementia/what-is-alzheimers" target="_blank" rel="nofollow">Alzheimer's</a>, dementia and other brain conditions. 

</p>

