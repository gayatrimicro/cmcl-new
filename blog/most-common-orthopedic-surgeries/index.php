                      <h1 class="blogh1">LEARN ABOUT MOST COMMON ORTHOPEDIC SURGERY</h1>
                    
                     <p>Orthopedic surgery deals with the pains and injuries involving muscles and skeletons in the human body that cannot be resolved by using <a href="https://www.betterhealth.vic.gov.au/health/ConditionsAndTreatments/medical-procedures-non-surgical" target="_blank" rel="nofollow"> non-surgical methods</a>. Although there are a variety of orthopedic problems which can be treated by surgeries there are some major disorders that can  impede movements treated by the orthopedic surgeons.
                     </p>
                     <p>Here are the top 4 most common orthopedic surgeries:</p>
                     <h2 class="blogh2">
                       Total Joint Replacement
                     </h2>
                     <p>Total joint replacement is a surgical procedure that refers to the removal and replacement of the damaged parts of a joint which is badly affected by accident or arthritis. The replacement of the damaged part is done with an artificial part called prosthesis which is made of metal, plastic or ceramic. The use of prostheses bring back the normal movements in the patients because they are designed to replicate the defective parts accurately. </p>
<p>
 Hip and Knee replacements are two of the most commonly performed <a href="https://www.niams.nih.gov/health-topics/joint-replacement-surgery" target="_blank" rel="nofollow"> joint replacements</a> on account of a large number of arthritis cases but other joint replacements such as the ankle, wrist, and elbow are also performed.
</p>

<h2 class="blogh2">
  Total Shoulder Replacement
</h2>
<p>
 Although it may be perceived as a painful procedure it’s a great relief for the patients and they feel much better after recovery. This surgical procedure makes use of metal or plastic implants to replace damaged bones and cartilage.  
</p>
<h2 class="blogh2">
  Spine Surgery
</h2>
<p>
  There are some compelling reasons for spine surgeries such as -</p>
<p>
  <ol>
<li><b>To take pressure off the nerve</b> - There can be pain, tingling, numbness, weak muscles and other more severe symptoms due to compression of nerves within the spinal column which can cause permanent impairment. <a href="https://www.spineuniverse.com/treatments/surgery" target="_blank" rel="nofollow">Spinal Surgeries</a> can provide permanent solutions.     </li>
<li><b>To stabilize an unstable spine</b> - The spine is the main part of the skeleton that provides the link between the upper and lower parts of the body and facilitates functions of the central nervous system. It also enables the body to stand erect and stay active by holding together the spinal ligaments, muscles, disc and bones<br>An unstable and weak spine cannot maintain its natural form under normal conditions. Unstable spine conditions include spinal fractures, spondylitis, or even extreme disc injuries. ALIF, PLIF, and Lateral Interbody Fusion are the major spine surgery methods by fusing the unstable parts of the spine.
</li>
<li><b>To realign a crooked spine </b>- Natural curvature of the spine is impaired by conditions like scoliosis, kyphosis, and flat-back syndrome. If the conditions are not improved by applying non-operative measures surgical interventions are resorted to, for getting the spine back to the original position.</li>

</ol>
</p>
<h2 class="blogh2">
  ACL Reconstruction:
</h2>
<p>
ACL is the short form of Anterior Cruciate Ligament which is the major stabilizing ligament or a band of tissues within the knee. <a href="https://orthoinfo.aaos.org/en/treatment/acl-injury-does-it-require-surgery/" target="_blank" rel="nofollow"> ACL injury</a> happens mostly with the sportspersons who are frequently engaged in running, jumping, or sharply turning. </p>
<p>
ACL reconstruction involves replacing the torn ligament with the tissues from the same patient or the tissues taken from an organ donor.
</p>
