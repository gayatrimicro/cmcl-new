<p>While Gastronomy and Jejunostomy require small surgeries for placement, NG - tubes or NJ - tubes do not require surgery and can be inserted by skilled hands and may cause some discomfort.
</p>

<h2 class="blogh2">Gastronomy feeding tubes </h2>
<p>Also called G - tubes or <a href="../peg-tubes-overview" target="_blank">PEG tubes</a>, these are recommended for use with both children and adults who require tube feeding for a long time. Gastronomy feeding tubes may also be used to avert a dangerous condition called aspiration. Aspiration occurs when one breathe some of the food or drink into the lungs due to a problem with the normal swallowing process. A PEG tube may be recommended for the following conditions:
</p>
<p>
  <ul>
<li>Congenital defects of mouth, esophagus, stomach, or intestines.</li>
<li>Swallowing and sucking difficulties</li>
<li>Failure to thrive</li>
<li>Stroke</li>
<li>Cancer of the mouth or esophagus </li>

  </ul>
</p>

<h2 class="blogh2">Preparations for Gastronomy procedure</h2>
<p>Gastronomy feeding procedure is performed in a hospital or clinic. At CMC Lancaster our specialists will ask you about your current medications including the blood thinners. Any type of blood thinner or anti-inflammatory medications must not be taken for at least a week or so before the gastronomy procedure. </p>

<p>Our specialists will also know if you are pregnant or have certain conditions such as:
</p>

<p>
  <ul>
<li>Heart conditions</li>
<li>Lung conditions</li>
<li>Allergies</li>
<li>Diabetes</li>

  </ul>
</p>


<p>Your medications (including the insulins in case you are a diabetic) for the above will have to be adjusted on the day of the procedure. You will abstain from eating at least for eight hours before the procedure. Also, you will be advised to bring someone who can escort you home after your release because the procedure will be performed under anesthesia which may make you drowsy for some time. You will be released most likely on the same day as the procedure. 
</p>


