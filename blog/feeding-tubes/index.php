                      <h1 class="blogh1">Feeding Tube: A life-saving alternative to natural feeding</h1>
                    
                     <p>Enteral feeding or tube feeding provides a life-saving alternative to natural feeding. It helps one get nutrition if he/she is having trouble eating naturally due to difficulties in swallowing or some type of surgery that forbids eating until recovery. This way, food in liquid form is delivered directly into the stomach or small intestine to help the body with the supply of sufficient nutrients.  </p>

<h2 class="blogh2">
 Who can receive tube feeding?
</h2>
<p>
 Doctors may suggest tube feeding for patients of all ages including infants and very olds. People can survive well through this process. Depending upon the illness, tube feeding may be required for a temporary period or it may continue until the patient expires. 
 </p>
  
<h2 class="blogh2">Different ways of enteral feeding</h2>
<p>There are two ways of delivering liquid foods into the body using feeding tubes. They are:
</p>
<p>
  <ul>
<li>Inserting tubes through the nose - If the requirement is temporary(for up to a month), usually this type of feeding is recommended. For delivery of food to the stomach, the type of tube used is called the nasogastric tube or NG - tube. Similarly, for delivery of food to the small intestine, it is called a nasojejunal tube or NJ - tube. </li>
<li>
Inserting tubes through the skin - Doctors recommend this procedure if the requirement is for a longer time. The tube is inserted through a small incision near the abdomen and extended either to the stomach(gastrostomy) or to the small intestine(jejunostomy). The procedure is done endoscopically.</li>

  </ul>
</p>

