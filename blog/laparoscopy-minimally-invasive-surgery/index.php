                      <h1 class="blogh1">LAPAROSCOPY: MINIMALLY INVASIVE SURGERY

</h1>
                    
                   <p>Having to undergo a major surgery is stressful, given the common perception of uncertainties, pain, and long lay-offs associated with it. But that is no longer a valid fear today for the majority of medical conditions, thanks to revolutionary progress in medical technology.</p>
<p>The age-old open surgical procedures with large incisions are being overwhelmingly replaced with minimally invasive surgeries that offer outstanding benefits over the conventional procedure in terms of:
  <ul>
<li>Smaller incisions and scars.</li>
<li>Shorter hospital stays and recovery times.</li>
<li>Lower risk of complications.</li>
<li>Less pain and discomfort.</li>
<li>Less scarring.</li>
<li>Reduced blood loss.</li>

  </ul>
                     </p>
                     <p>Shed your worries, if your treatment requires surgeries in the chest, abdomen, groin, and pelvis. A minimally invasive surgery (also called keyhole surgery) has a very high success rate with rare serious complications. You will be released most likely on the same day and back in your normal life in about a couple of weeks.
</p>

<h2 class="blogh2">What is a minimally invasive surgery?</h2>
 <p>The term ‘minimally invasive’ refers to very small to no incision compared to the sizes of cuts needed in open surgeries( 6” -8” diameter). </p>
<p>Laparoscopy is a minimally invasive procedure, performed in the abdomen and pelvic regions, requires only a few tiny holes of ¼” to ½” diameter.
</p>
