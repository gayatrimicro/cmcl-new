                     <p>The human female reproductive system is built on internal as well as external sex organs. There can be various types of female diseases that may require the removal of certain sex organs, as part of the treatments.  </p>
                     <p> 
Hysterectomy is a type of surgical procedures that are used to remove mainly the internal sex organs such as the uterus, ovaries, and fallopian tubes. Some treatments may also require to remove a portion of the vagina, which is an external sex organ.  
                     </p>

                 
                    
                     <h2 class="blogh2">
                      When to go for a hysterectomy?


                     </h2>
                     <p>Hysterectomy is recommended when all other treatment options have failed. The patient(of any age) on whom hysterectomy is performed, loses menstruation as well as child-bearing capacity permanently. Below are certain conditions that may require hysterectomy for a cure:  
 
</p>
<p>
  <ul>
<li>Abnormal menstrual bleeding with or without other symptoms such as pain and stomach cramps, that significantly affects the quality of life. </li>

<li><a href="https://www.ncbi.nlm.nih.gov/pmc/articles/PMC3902555/" target="_blank" rel="nofollow"> Severe endometriosis</a>, when uterine cells spread over to other areas of the body and the reproductive system such as the ovaries, fallopian tubes, bladder, and rectum, causing pain, heaviness, irregular periods, etc.</li>

<li>Leiomyomas or uterine fibroids (non-cancerous tumors) that have increased in size, are causing heavy or painful periods, pelvic pain, frequent constipation or urination, pain during sex, etc. </li>

<li>Increased pelvic pain related to the uterus but not controlled by other treatment</li>

<li>Prolapse of the uterus (when the uterus is dropped into the vaginal canal due to weakened support muscles) that can lead to ‘leaking urine’ or difficulty with bowel movements, back pain, and difficulties during sex. </li>

<li>Cervical cancer, ovarian cancer, cancer of the fallopian tubes, and uterine cancer or abnormalities that may lead to these cancers.</li>
  </ul>
</p>

<h2 class="blogh2">
  Different methods of hysterectomy:

</h2>
<p>
  <ul>
 <li>Supracervical hysterectomy -  To remove the upper part of the uterus, but not the cervix, which is the lower part of the uterus. 
 </li>
<li>Total hysterectomy - To remove the entire uterus ( including cervix ).</li>
<li>Hysterectomy with <a href="https://www.cancer.gov/publications/dictionaries/cancer-terms/def/bilateral-salpingo-oophorectomy" target="_blank" rel="nofollow"> bilateral salpingo-oophorectomy</a> - To remove the uterus, cervix, fallopian tubes (salpingo) and ovaries (oophor). 
Also there is a fourth type of hysterectomy, known as </li>
<li>Radical hysterectomy with bilateral salpingo-oophorectomy which is a procedure to remove the uterus, cervix, fallopian tubes, ovaries, the upper part of the vagina and some surrounding tissue, and lymph nodes. A radical hysterectomy may be performed to treat cervical or uterine cancer.
</li>
</ul>
 
</p>



<h2 class="blogh2">
 How is a hysterectomy performed?
</h2>
<p>
  There can be different ways of performing a hysterectomy, from minimal to most invasive methods with varying degrees of risks and benefits. However, a minimally invasive hysterectomy using a laparoscope is much more popular because of minimum risk and faster recovery.  </p>
  <p>
Nearly all types of hysterectomy are performed through laparoscopy. The patients in most cases can go home on the same day. 

</p>
<p>A <a href="https://www.fascrs.org/patients/disease-condition/laparoscopic-surgery-what-it" target="_blank" rel="nofollow">laparoscope</a> is a long, thin tube with a high-intensity light and a high-resolution camera at the front. The instrument is inserted through a small incision in the abdomen, just below the navel. As it moves along, the camera sends images to a video monitor.</p>
<p>
This way the surgeon can view the inside of the abdomen and exact locations of various organs. Once this is done, the surgeon inserts other surgical tools through other small incisions in the abdomen or vagina to remove the womb (uterus), cervix, and/or other reproductive organs. Before inserting the laparoscope, the abdomen is inflated by using carbon-dioxide. This is done for easy movements of the instrument. The entire operation is performed under the local anesthesia.</p>

<h2 class="blogh2">
 Recovery from a hysterectomy

</h2>
<p>
Although the patient will be discharged on the same day of the procedure, the patient is advised to come for a check-up four to six weeks after the surgery.</p>
<p>
As long as the patient doesn’t report any fever or heavy bleeding, recovery seems to be smooth. </p>
<p>
However, caution to be maintained for almost all types of laparoscopic hysterectomy that the patient shouldn’t try lifting weights higher than 4-5 kgs. during the first six weeks. 
Another precaution is very important for the patient is that she must not be engaged in any kind of vaginal penetration for at least 8 weeks from the date of surgery.  

</p>




