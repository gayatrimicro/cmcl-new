<h2 class="blogh2">
 1. Scoliosis Surgery

</h2>
<p>
 If you observe an abnormal curve in spine area, it is a symptom of scoliosis. There are different types of scoliosis based on the age and cause of its development. Most of the time scoliosis occurs because of the curvature of our spine. </p>


<p>We diagnose the issue of scoliosis using imaging techniques like MRI, CT Scans and X-rays. Depending upon its severity and to prevent this condition from getting worse, we treat with bracing or in extreme conditions surgery for scoliosis.  

 </p>




<h2 class="blogh2">2. Spine fusion Surgery</h2>

<p>The spine fusion surgery procedure is used for connecting two or more spine vertebrae, where the motion between them is eliminated. In this procedure the design is to copy the normal healing procedure of bones broken. </p>
<p>In this procedure, the surgeon places bone like material in the space between to spine vertebrae. Along with it, metal screws, plates, and rods could be used to hold the two to form a whole single unit. 
</p>



<h2 class="blogh2">3.  Micro-decompression (Microdiscectomy) Surgery</h2>

<p>Whenever a patient suffers from pain due to pressure on its spine nerve root, also called herniated disc - we perform microdiscectomy or micro-decompression surgery procedure. 
</p>
<p class="blogh2">
 This technique of surgery has been considered as gold standard in the removal of a herniated portion of spine disc that usually presses on the nerves. Our spine surgeons have a long experience in dealing with this condition.  
</p>

<h2 class="blogh2">4. Laminectomy (Decompression Surgery)</h2>
<p>
Decompression surgery is a procedure that will create space between your spine bones through the removal of lamina. Lamina is the back portion of vertebrae that covers spine canal. 
</p>

<p>This procedure is also known as decompression surgery through which your spine column is enlarged to relive pressure on your nerves or spinal cord, a condition that occurs due to bony overgrowths within the spinal canal.
</p>


                  
<p class="blogh2"><strong>Cervical Spine Surgery</strong> - This surgery is generally done on elective basis to treat two of these following conditions: </p>

<p class="blogh2"><strong>Spine instability</strong> - Cured using fusion surgery
</p>

<p class="blogh2"><strong>Spine cord or nerve impingement</strong> - Using decompression surgery
</p>

<p class="blogh2">
These two procedures are most of the time combined, owing to the fact that decompression tends to de-stabilize the spine and create a requirement for fusion to provide stability.
</p>

